int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll T; cin >> T;
  for (ll _t = 0; _t < T; _t++) {

    vector<ll> op;
    ll N; cin >> N;
    vector<ll> P(N + 1);

    ll turn = 1;
    auto operate = [&](ll n) -> void {
      op.push_back(n);
      swap(P[n], P[n + 1]);
      turn++;
    };

    for (ll i = 1; i <= N; i++) cin >> P[i];
    for (ll i = N; i >= 5; i--) {
      if (P[i] == i) continue;
      ll p = 1;
      for ( ; p <= N; p++) { if (P[p] == i) break; }
      if (turn % 2 != p % 2) {
        if (turn % 2 == 1) {
          if (P[1] != i && P[2] != i) operate(1);
          else operate(3);
        }else {
          if (P[2] != i && P[3] != i) operate(2);
          else operate(4);
        }
      }
      for (; p < i; p++) operate(p);
      DLOG(i, P);
    }
    if (N >= 4) {
      if (P[4] < 4) {
        ll i = 4;
        ll p = 1;
        for ( ; p <= N; p++) { if (P[p] == i) break; }
        if (turn % 2 != p % 2) {
          if (p == 1) operate(2);
          if (p == 2) operate(3);
          if (p == 3) {
            operate(2); operate(3);
            p = 2;
          }
        }
        for (; p < i; p++) operate(p);
      }
    }
    if (N >= 3) {
      assert(P[1] >= 1 && P[1] <= 3 && P[2] >= 1 && P[2] <= 3 &&
             P[3] >= 1 && P[3] <= 3);
      while (P[1] != 1 || P[2] != 2 || P[3] != 3) {
        DLOGK(P);
        if (turn % 2 == 0) operate(2);
        else operate(1);
      }
    }
    if (N == 2 && P[1] == 2) operate(1);

    cout << op.size() << "\n";
    for (ll x : op) { cout << x << " "; }
    cout << "\n";

    
  }

  return 0;

}

