int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  const ll NONE = -100;
  const ll UP = -101;
  const ll DOWN = -102;

  ll N; cin >> N;
  vector<ll> ud(2*N, NONE);
  bool sane = true;
  for (ll i = 0; i < N; i++) {
    ll a, b; cin >> a >> b; a--; b--;
    if (a >= 0 && b >= 0 && a >= b) sane = false;
    if (a >= 0) {
      if (ud[a] != NONE) sane = false;
      ud[a] = b == -2 ? UP : b;
    }
    if (b >= 0) {
      if (ud[b] != NONE) sane = false;
      ud[b] = a == -2 ? DOWN : a;
    }
  }
  if (!sane) {
    DLOG("insane");
    cout << "No\n";
    return 0;
  }

  auto chk = [&](auto rF, ll beg) -> bool {
    if (beg == 2 * N) return true;

    auto sub = [&](ll cont) -> bool {
      for (ll i = 0; i < cont; i++) {
	ll xu = beg + i;
	ll xd = xu + cont;
	if (ud[xu] == DOWN) {
	  return false;
	}else if (ud[xu] >= 0) {
	  if (ud[xu] != xd) return false;
	}else if (ud[xu] == UP) {
	  if (ud[xd] != NONE) return false;
	}else if (ud[xd] >= 0) {
	  return false;
	}else if (ud[xd] == UP) {
	  return false;
	}
      }
      return true;
    };

    for (ll cont = 1; beg + 2 * cont <= 2 * N; cont++) {
      if (!sub(cont)) continue;
      DLOG("rec: beg=", beg, "cont=", cont);
      if (rF(rF, beg + 2 * cont)) return true;
      DLOG("rec fail: beg=", beg, "cont=", cont);
    }
    return false;
  };

  cout << (chk(chk, 0) ? "Yes" : "No") << endl;

  return 0;
}

