int main(int argc, char *argv[]) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  using Pair = pair<ll, ll>;
  using Tri = tuple<ll, ll, ll>;

  ll N, M; cin >> N >> M;
  vector<vector<Pair>> nbr(N);
  for (ll i = 0; i < M; i++) {
    ll c, a, b;
    cin >> c >> a >> b;
    nbr.at(a).emplace_back(b, c);
    nbr.at(b).emplace_back(a, c);
  }
  // ll K = sqrt(2*N) + 10;
  vector<ll> cost(N, LLONG_MAX);
  priority_queue<Tri, vector<Tri>, greater<Tri>> pque;
  pque.emplace(0, 0, 0);
  while (!pque.empty()) {
    ll numB, len, pos; tie(numB, len, pos) = pque.top(); pque.pop();
    ll& v = cost.at(pos);
    DLOG("len, numB, pos, v", len, numB, pos, v);
    if (len < v) {
      v = len;
      for (Pair p : nbr.at(pos)) {
	if (p.second == 0) pque.emplace(numB, len + 1, p.first);
	else               pque.emplace(numB + 1, len + numB + 1, p.first);
      }
    }
    DLOG("pque", pque);
  }
  for (ll i = 0; i < N; i++) {
    cout << cost.at(i) << "\n";
  }
  
  
  return 0;
}

