int naive(istream& cin, ostream& cout) {
  auto solve = [&]() -> ll {
    ll N, AX, BX, AY, BY; cin >> N >> AX >> BX >> AY >> BY;
    ll ret = 0;
    for (ll i = 1; i <= N; i++) {
      ll levX = AX + divFloor(i, BX);
      ll levY = AY + divFloor(i, BY);
      if (levX == levY) ret++;
    }
    return ret;
  };


  ll T; cin >> T;
  for (; T > 0; T--) cout << solve() << "\n";
  return 0;
}
int body(istream& cin, ostream& cout) {

  auto solve = [&]() -> ll {
    ll N, AX, BX, AY, BY; cin >> N >> AX >> BX >> AY >> BY;
    if (BX == BY) return (AX == AY ? N : 0);
    if (BX < BY) { swap(BX, BY); swap(AX, AY); }
    ll A = AX - AY;
    if (A < 0) return 0;
    ll y0 = max(0LL, divCeil(A*BY - BX + 1, BX - BY));
    ll ylim = divFloor(N, BY) - A;
    if (y0 > ylim) return 0;
    ll c1 = (y0*BX + BX) - (y0*BY + A*BY);
    ll d1 = BX - BY;
    ll k1 = max(0LL, divCeil(BY - c1, d1));
    if (y0 + k1 > ylim) k1 = ylim - y0 + 1;
    ll sum1 = k1 * (2 * c1 + (k1 - 1) * d1) / 2;
    ll y1 = min(ylim, divFloor(A*BY + BY - 1, BX - BY));
    ll c2 = y1*BY + A*BY + BY - 1 - y1*BX + 1;
    ll k2 = max(0LL, divCeil(BY - c2, d1));
    ll sum2 = k2 * (2 * c2 + (k2 - 1) * d1) / 2;
    ll sum3 = ((y1 - y0 + 1) - (k1 + k2)) * BY;
    ll sum = sum1 + sum2 + sum3;
    if (A == 0) sum--;
    if (y1 == ylim) {
      ll Ymax = ylim * BY + A * BY + BY - 1;
      ll Xmin = ylim * BX;
      ll Xmax = ylim * BX + BX - 1;
      ll p = max(N + 1, Xmin);
      ll q = min(Ymax, Xmax);
      ll exc = max(0LL, q - p + 1);
      DLOGK(exc);
      sum -= exc;
    }
    DLOGK(sum, sum1, sum2, sum3);
    DLOGK(ylim, y0, c1, d1, k1);
    DLOGK(y1, c2, k2);
    return sum;
  };

  ll T; cin >> T;
  for (; T > 0; T--) cout << solve() << "\n";
  return 0;
}

