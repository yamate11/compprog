int naive(istream& cin, ostream& cout) {
  ll N; cin >> N;
  string S; cin >> S;

  auto nnxt = [](char c1, char c2) -> char {
    if (c1 > c2) swap(c1, c2);
    if (c1 == 'A' && c2 == 'B') return 'C';
    if (c1 == 'A' && c2 == 'C') return 'B';
    if (c1 == 'B' && c2 == 'C') return 'A';
    assert(0);
  };

  set<string> ans;
  ans.insert(S);
  auto func = [&](auto rF, string s) -> void {
    // DLOGK(s);
    ll n = s.size();
    for (ll i = 0; i < n - 1; i++) {
      if (s[i] == s[i + 1]) continue;
      char c = nnxt(s[i], s[i + 1]);
      string t = s.substr(0, i) + string(1, c) + s.substr(i + 2);
      ans.insert(t);
      rF(rF, t);
    }
  };
  func(func, S);
  cout << ans.size() << endl;
  return 0;
}

int body(istream& cin, ostream& cout) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N; cin >> N;
  vector<ll> vec(N);
  {
    string S; cin >> S;
    for (ll i = 0; i < N; i++) vec[i] = S[i] - 'A' + 1;
  }
  vector<ll> psum(N + 1);
  for (ll i = N - 1; i >= 0; i--) psum[i] = psum[i + 1] ^ vec[i];
  {
    ll i = 0;
    for ( ; i < N - 1 && vec[i] == vec[i + 1]; i++);
    if (i == N - 1) {
      cout << 1 << endl;
      return 0;
    }
  }
  vector<ll> nxt(N);
  for (ll i = 0; i < N; i++) nxt[i] = i + 1;
  {
    ll en = N - 1;
    for (ll i = N - 2; i >= 0; i--) {
      if (vec[i] != vec[i + 1]) {
        en = i;
      }else {
        if (en - i >= 4) {
          if ((en - i) % 2 == 0) nxt[i + 1] = en;
          else                   nxt[i + 1] = en - 1;
        }
      }
    }
  }
  vector<Fp> tbl(N + 1);
  for (ll idx = N - 1; idx >= 0; idx--) {
    Fp cnt = 0;
    for (ll c = 1; c <= 3; c++) {
      ll i = idx;
      ll d = vec[idx];
      for ( ; d != c && i + 1 < N; i = nxt[i], d = d ^ vec[i]);
      ll this_cnt;
      if (d != c) {
        this_cnt = 0;
        DLOGKL("d!=c", idx, i, c, d, this_cnt);
      }else {
        this_cnt = tbl[i + 1];
        if (psum[i + 1] == 0) {
          this_cnt += 1;
          DLOGKL("psum[i+1]==0", idx, i, c, d, this_cnt);
        }else {
          DLOGKL("ordinary", idx, i, c, d, this_cnt);
        }
      }
      cnt += this_cnt;
      tbl[idx] = cnt;
    }
  }
  DLOGK(tbl);
  cout << tbl[0] << endl;

  return 0;
}

