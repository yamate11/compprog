using Pair = pair<ll, ll>;

int main(int argc, char *argv[]) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, A, B; cin >> N >> A >> B;
  vector<Pair> ap;
  for (ll i = 0; i < N; i++) {
    ll a; cin >> a;
    ap.emplace_back(a, 0);
  }
  sort(ap.begin(), ap.end());
  if (A == 1) {
    for (Pair x : ap) cout << x.first << "\n";
    return 0;
  }
  double lim = 32.0 * log(2.0) / log((double)A);
  vector<ll> powerA(lim+1);
  powerA.at(0) = 1;
  for (ll i = 1; i <= lim; i++) powerA.at(i) = powerA.at(i-1) * A;
  auto comp = [&](Pair p, Pair q) -> bool {
    ll r = q.second - p.second;
    if (r > lim) return false;
    if (r < -lim) return true;
    if (r > 0) return p.first > q.first * powerA.at(r);
    if (r < 0) return p.first * powerA.at(-r) > q.first;
    return p.first > q.first;
  };

  DLOG("lim", lim);
  DLOG("powerA", powerA);
  // cout << comp(make_pair(1, 2), make_pair(200000, 0)) << endl;
  // cout << comp(make_pair(200000, 0), make_pair(1, 2)) << endl;

  Pair largest = ap.at(N-1);
  priority_queue<Pair, vector<Pair>, decltype(comp)> pque(comp);
  for (Pair x : ap) pque.push(x);
  ll cnt = 0;
  while (true) {
    Pair x = pque.top(); pque.pop();
    pque.emplace(x.first, x.second + 1);
    cnt++;
    if (x == largest || cnt == B) break;
  }
  DLOG("after sim", pque);
  ll rest = B - cnt;
  ll r1 = rest / N;
  ll r2 = rest % N;
  for (ll i = 0; i < r2; i++) {
    Pair x = pque.top(); pque.pop();
    pque.emplace(x.first, x.second + 1);
  }
  while (!pque.empty()) {
    Pair x = pque.top(); pque.pop();
    cout << Fp(x.first) * power(Fp(A), r1 + x.second) << "\n";
  }

  return 0;
}

