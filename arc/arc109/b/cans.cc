template<typename T>
T binsearch(auto check, T yes, T no, T err = (T)1) {
  while (abs(yes - no) > err) {
    T mid = (yes + no) / (T)2;
    if (check(mid)) yes = mid;
    else            no  = mid;
  }
  return yes;
}

// ---- end binsearch.cc
// @@ !! LIM  -- end mark --

int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll n; cin >> n;

  auto check = [&](ll k) -> bool {
    return k * (k + 1) / 2 <= n + 1;
  };
  ll k = binsearch<ll>(check, 1, ll(sqrt(2) * 1e9 + 10));
  cout << n - k + 1 << endl;

  return 0;
}

