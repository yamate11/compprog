template <typename T>
struct LinTr {
  T p;
  T q;
  LinTr(int i) : p(i), q(0) {}
  LinTr(T _p, T _q) : p(_p), q(_q) {}
  LinTr& operator *=(const LinTr& g) {
    q += p * g.q;
    p *= g.p;
    return *this;
  }
  LinTr operator *(const LinTr& g) const { return LinTr(p, q) *= g; }
  T operator ()(T x) { return p * x + q; }
};

template <typename T>
ostream& operator<<(ostream& os, const LinTr<T>& f) {
  return os << "{" << f.p << "x+" << f.q << "}";
}


