// ----------------------------------------------------------------------
int main() {

  {
    Ftwo zero(0);
    Ftwo one(1);

    assert(zero + zero == zero);
    assert(zero + one == one);
    assert(one + zero == one);
    assert(one + one == zero);
    assert(zero - zero == zero);
    assert(zero - one == one);
    assert(one - zero == one);
    assert(one - one == zero);
    assert(zero * zero == zero);
    assert(zero * one == zero);
    assert(one * zero == zero);
    assert(one * one == one);
    assert(zero / one == zero);
    assert(one / one == one);
  }

  cerr << "Test Done.\n";

}
