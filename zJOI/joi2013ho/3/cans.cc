int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll M, N, K; cin >> M >> N >> K;
  vector<ll> X(K), Y(K);
  vector<vector<ll>> recX(M), recY(N);
  bool incl_goal = false;
  for (ll i = 0; i < K; i++) {
    ll x, y; cin >> x >> y; x--; y--;
    X[i] = x;
    Y[i] = y;
    recX[x].push_back(y);
    recY[y].push_back(x);
    if (x == M - 1 && y == N - 1) incl_goal = true;
  }
  if (! incl_goal) {
    recX[M - 1].push_back(N - 1);
    recY[N - 1].push_back(M - 1);
  }
  
  for (ll x = 0; x < M; x++) sort(recX[x].begin(), recX[x].end());
  for (ll y = 0; y < N; y++) sort(recY[y].begin(), recY[y].end());
  using stb = tuple<ll, ll, ll>;
  using sta = pair<ll, stb>;

  auto nbr_cells = [&](stb& cell) {
    DLOGKL("nbr_cells", cell);
    vector<pair<ll, stb>> ret;
    auto [vert, x, y] = cell;
    ret.emplace_back(1, stb(1 - vert, x, y));
    ll p = vert ? y : x;
    ll oth = vert ? x : y;
    vector<ll>& vv = vert ? recX[x] : recY[y];
    auto the_cell = [&](ll pthis, ll pother) -> stb {
      if (vert) return stb(vert, pother, pthis);
      else      return stb(vert, pthis, pother);
    };
    ll idx = lower_bound(vv.begin(), vv.end(), p) - vv.begin();
    if (idx + 1 < (ll)vv.size()) {
      ret.emplace_back(vv[idx+1] - p, the_cell(vv[idx+1], oth));
    }
    if (idx - 1 >= 0) {
      ret.emplace_back(p - vv[idx-1], the_cell(vv[idx-1], oth));
    }
    DLOGKL("ret nbr_cells", ret);
    return ret;
  };

  const ll big = 1e15;
  auto enc = [&](stb& cell) -> ll {
    auto [isvert, x, y] = cell;
    return isvert + 2 * (x + M * y);
  };
  unordered_map<ll, ll> dist;
  auto get_dist = [&](stb& cell) -> ll {
    auto it = dist.find(enc(cell));
    if (it == dist.end()) return big;
    return it->second;
  };

  if (M == 1) {
    cout << N - 1 << endl;
    return 0;
  }
  if (recX[0].empty()) {
    cout << -1 << endl;
    return 0;
  }

  auto is_goal = [&](stb cell) -> bool {
    auto [dum, x, y] = cell;
    return x == M - 1 && y == N - 1;
  };
  priority_queue<sta, vector<sta>, greater<sta>> pque;
  ll y0 = recX[0][0];
  stb start(1, 0, y0);
  dist[enc(start)] = y0;
  pque.emplace(y0, start);
  while (! pque.empty()) {
    auto [d, cell] = pque.top(); pque.pop();
    DLOGK(d, cell);
    if (d > get_dist(cell)) continue;
    if (is_goal(cell)) {
      cout << d << endl;
      return 0;
    }
    for (auto& [diff, cell3] : nbr_cells(cell)) {
      if (d + diff < get_dist(cell3)) {
        dist[enc(cell3)] = d + diff;
        pque.emplace(d + diff, cell3);
      }
    }
  }
  cout << -1 << endl;
  return 0;
}

