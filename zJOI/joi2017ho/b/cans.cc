int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, M, K; cin >> N >> M >> K;
  ll A, B, C; cin >> A >> B >> C;
  ll T; cin >> T;
  vector S(M, 0LL);
  for (ll i = 0; i < M; i++) {
    ll s; cin >> s; s--;
    S[i] = s;
  }
  ll z = 0;
  auto func = [&](ll ns, ll rem) -> tuple<ll, ll, ll> {
    ll u = rem / A + 1;
    if (u >= ns) return {ns, -1, -1};
    else return {u, ns - u, rem - C * u};
  };
  using sta = tuple<ll, ll, ll>;
  priority_queue<sta> pque;
  for (ll i = 0; i < M; i++) {
    ll rem = T - B * S[i];
    if (rem < 0) break;
    ll ns = i == M-1 ? 1 : S[i + 1] - S[i];
    auto [m, newNs, newRem] = func(ns, rem);
    DLOG("i=", i, "m=", m, "newNs=", newNs, "newRem=", newRem);
    z += m;
    if (newNs > 0 && newRem >= 0) {
      auto [m2, newNs2, newRem2] = func(newNs, newRem);
      pque.emplace(m2, newNs2, newRem2);
    }
  }
  DLOG("pque=", pque);
  ll y = 0;
  for (ll _k = 0; _k < K - M; _k++) {
    if (pque.empty()) break;
    auto [m, ns, rem] = pque.top(); pque.pop();
    y += m;
    if (ns > 0 && rem >= 0) {
      auto [m2, ns2, rem2] = func(ns, rem);
      pque.emplace(m2, ns2, rem2);
    }
  }
  DLOG("z=", z, "y=", y);
  cout << z + y - 1 << endl;
  return 0;
}

