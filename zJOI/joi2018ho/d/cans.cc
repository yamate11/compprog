int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, M, S, T, U, V; cin >> N >> M >> S >> T >> U >> V;
  S--; T--; U--; V--;
  using sta = pair<ll, ll>;
  vector<vector<sta>> nbr(N);
  for (ll i = 0; i < M; i++) {
    ll a, b, c; cin >> a >> b >> c; a--; b--;
    nbr[a].emplace_back(b, c);
    nbr[b].emplace_back(a, c); }
  const ll big = 1e16;
  auto dij = [&](auto rF, ll st) -> vector<ll> {
    vector<ll> dist = vector<ll>(N, big);
    using stb = pair<ll, ll>;
    priority_queue<stb, vector<stb>, greater<stb>> pque;
    dist[st] = 0;
    pque.emplace(0, st);
    while (!pque.empty()) {
      auto [d, x] = pque.top(); pque.pop();
      if (d > dist[x]) continue;
      for (auto [y, s] : nbr[x]) {
        ll newD = d + s;
        if (newD < dist[y]) {
          dist[y] = newD;
          pque.emplace(newD, y); }}}
    return dist; };
  auto fromS = dij(dij, S);
  auto fromT = dij(dij, T);
  vector<bool> stst(N);
  vector<vector<ll>> nbr2(N);
  for (ll i = 0; i < N; i++) stst[i] = (fromS[i] + fromT[i] == fromS[T]);
  for (ll i = 0; i < N; i++) if (stst[i]) for (auto [x, s] : nbr[i]) {
        if (stst[x] && fromT[x] < fromT[i]) nbr2[i].push_back(x); }
  DLOGK(nbr2);
  vector<vector<ll>> fuv(2);
  fuv[0] = dij(dij, U);
  fuv[1] = dij(dij, V);
  DLOGK(fuv[0]);
  DLOGK(fuv[1]);
  vector<vector<ll>> memo(3, vector<ll>(N, -1));
  auto func = [&](auto rF, ll typ, ll x) -> ll {
    ll& ret = memo[typ][x];
    if (ret < 0) {
      if (typ < 2) { // U or V
        ret = fuv[typ][x];
        for (auto y : nbr2[x]) ret = min(ret, rF(rF, typ, y));
      } else if (typ == 2) {
        ret = fuv[0][x] + rF(rF, 1, x);
        ret = min(ret, fuv[1][x] + rF(rF, 0, x));
        for (auto y : nbr2[x]) ret = min(ret, rF(rF, 2, y));
      }
      DLOGK(typ, x, ret);
    }
    return ret;
  };
  ll via = func(func, 2, S);
  ll dir = fuv[0][V];
  cout << min(via, dir) << endl;
  return 0;
}

