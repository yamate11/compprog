int naive(istream& cin, ostream& cout) {
  ll N; cin >> N;
  vector<ll> A(N), B(N);
  for (ll i = 0; i < N; i++) cin >> A[i];
  for (ll i = 0; i < N; i++) cin >> B[i];
  string s; cin >> s;
  vector<ll> X(N);
  for (ll i = 0; i < N; i++) cin >> X[i];
  if (s == "No") {
    DLOG("naive -- OK (no)");
    cout << "OK\n";
    return 0;
  }
  for (ll i = 0; i < N; i++) {
    if (A[i] == X[i]) {
      cout << "NG\n";
      DLOG("naive -- NG");
      return 0;
    }
  }
  sort(B.begin(), B.end());
  sort(X.begin(), X.end());
  if (B != X) {
    cout << "NG\n";
    DLOG("naive -- NG");
    return 0;
  }
  cout << "OK\n";
  DLOG("naive -- OK");

  return 0;
}

int body(istream& cin, ostream& cout) {

  DLOG("body is called\n");
  ll N; cin >> N;
  DLOG("read N");
  vector<ll> numA(N), numB(N);
  for (ll i = 0; i < N; i++) {
    ll a; cin >> a; a--;
    numA[a]++;
  }
  for (ll i = 0; i < N; i++) {
    ll b; cin >> b; b--;
    numB[b]++;
  }
  DLOG("read A and B");
  ll nabmax = 0;
  ll imax = -1;
  for (ll i = 0; i < N; i++) {
    if (numA[i] + numB[i] > nabmax) {
      imax = i;
      nabmax = numA[i] + numB[i];
    }
  }
  if (nabmax > N) {
    cout << "No\n";
    return 0;
  }
  cout << "Yes\n";
  ll rem = N - nabmax;
  ll p = 0;
  ll q = 0;
  using sta = pair<ll, ll>;
  vector<sta> rec;
  while (rem > 0) {
    while (p == imax || numA[p] == 0) p = (p + 1) % N;
    ll j = 0;
    for (; j < N; j++) {
      ll r = (q + j) % N;
      if (r == p || r == imax || numB[r] == 0) continue;
      rec.emplace_back(p, r);
      numA[p]--;
      numB[r]--;
      rem--;
      q = r;
      break;
    }
    if (j == N) p++;
  }
  while (numA[imax] > 0) {
    while(q == imax || numB[q] == 0) q = (q + 1) % N;
    rec.emplace_back(imax, q);
    numA[imax]--;
    numB[q]--;
  }
  while (numB[imax] > 0) {
    while(p == imax || numA[p] == 0) p = (p + 1) % N;
    rec.emplace_back(p, imax);
    numA[p]--;
    numB[imax]--;
  }
  sort(rec.begin(), rec.end());
  for (ll i = 0; i < N; i++) {
    if (i > 0) cout << " ";
    cout << rec[i].second + 1;
  }
  cout << endl;
  DLOG("finish");
  return 0;
}

