int naive(istream& cin, ostream& cout) {
  ll N, M, Q; cin >> N >> M >> Q;
  using tpl1 = tuple<ll, ll, ll>;
  vector bus(N + 1, vector<tpl1>());
  for (ll i = 0; i < M; i++) {
    ll a, b, s, t; cin >> a >> b >> s >> t;
    bus[a].emplace_back(s, b, t);
  }
  for (ll i = 1; i <= N; i++) sort(bus[i].begin(), bus[i].end());
  for (; Q > 0; Q--) {
    ll x, y, z; cin >> x >> y >> z;
    ll town = y;
    ll time = x;
    while (true) {
      auto& sch = bus[town];
      ll p = lower_bound(sch.begin(), sch.end(), tpl1(time, -1, -1)) - sch.begin();
      if (p == (ll)sch.size()) {
        cout << town << "\n";
        break;
      }
      auto [stime, dest, etime] = sch[p];
      if (z <= stime) {
        cout << town << "\n";
        break;
      }else if (z <= etime) {
        cout << town << " " << dest << "\n";
        break;
      }
      town = dest;
      time = etime;
    }
  }
  return 0;
}
int body(istream& cin, ostream& cout) {
  ll N, M, Q; cin >> N >> M >> Q;
  using sta = tuple<ll, ll, ll, ll>;
  vector<sta> event;
  using stc = tuple<ll, ll>;
  vector<stc> businfo(M);
  for (ll i = 0; i < M; i++) {
    ll a, b, s, t; cin >> a >> b >> s >> t;
    a--; b--;
    s = s * 2 + 1;
    t = t * 2 + 1;
    businfo[i] = stc(a, b);
    event.emplace_back(s, 2, 0, i);
    event.emplace_back(t, 1, 0, i);
  }
  for (ll i = 0; i < Q; i++) {
    ll x, y, z; cin >> x >> y >> z;
    x = x * 2;
    z = z * 2;
    y--;
    event.emplace_back(x, 3, y, i);
    event.emplace_back(z, 4, 0, i);
  }
  sort(event.begin(), event.end());
  DLOGK(event);

  UnionFind uf(Q);

  using stb = pair<ll, ll>;
  vector<stb> current(Q, {-1, -1});
  vector<ll> townLeader(N, -1);
  vector<stb> ans(Q);
  vector<ll> onBus(M, -1);
  for (auto [tm, tp, a1, a2] : event) {
    if (tp == 1) {
      ll m = a2;
      auto [a, b] = businfo[m];
      ll person = onBus[m];
      onBus[m] = -1;
      if (person >= 0) {
        ll tl = townLeader[b];
        if (tl < 0) tl = person;
        else tl = uf.merge(person, tl);
        townLeader[b] = tl;
        current[tl] = {b, -1};
      }
    }else if (tp == 2) {
      ll m = a2;
      auto [a, b] = businfo[m];
      ll tl = townLeader[a];
      townLeader[a] = -1;
      if (tl >= 0) {
        current[tl] = {a, b};
        onBus[m] = tl;
      }
    }else if (tp == 3) {
      ll y = a1;
      ll i = a2;
      ll tl = townLeader[y];
      if (tl < 0) tl = i;
      else tl = uf.merge(i, tl);
      townLeader[y] = tl;
      current[tl] = {y, -1};
    }else if (tp == 4) {
      ll i = a2;
      ll j = uf.leader(i);
      ans[i] = current[j];
    }
  }
  for (ll i = 0; i < Q; i++) {
    auto [x, y] = ans[i];
    cout << x + 1;
    if (y >= 0) cout << " " << y + 1;
    cout << "\n";
  }

  return 0;
}

