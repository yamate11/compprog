struct complete : exception {};

const ll Queen = 1;
const ll King = 2;
const ll Rook = 3;
const ll Bishop = 4;
const ll Knight = 5;
vector<string> pname = {"", "Queen", "King", "Rook", "Bishop", "Knight"};

vector<BrdIdx> nbrD({{1,1},{-1,1},{-1,-1},{1,-1}});
vector<BrdIdx> nbrKn({{2,1},{1,2},{-1,2},{-2,1},
                      {-2,-1},{-1,-2},{1,-2},{2,-1}});

int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  random_device rd;
  mt19937 rng(rd());
  auto randrange = [&rng](ll i, ll j) -> ll {
    uniform_int_distribution<ll> dist(i, j - 1);
    return dist(rng);
  };

  Board<int> brd(8, 8, -1);
  Board<int> prepare(8, 8, -1);
  for (ll r = 0; r < 8; r++) {
    for (ll c = 0; c < 8; c++) {
      brd.at(r, c) = 0;
      prepare.at(r, c) = 0;
    }
  }

  auto effect = [&](BrdIdx bi, ll p, Board<int>& ref) -> ll {
    auto sub = [&](ll lim, const auto& dirs) -> ll {
      ll ret = 0;
      for (const BrdIdx& dir : dirs) {
        for (ll step = 1; step <= lim; step++) {
          BrdIdx cur = bi + step * dir;
          if (brd.at(cur) != 0) break;
          if (ref.at(cur) == 0) {
            ref.at(cur) = 1;
            ret++;
          }
        }
      }
      return ret;
    };
    ll r = 0;
    if      (p == Rook)   r = sub(8, BrdIdx::nbr4);
    else if (p == Bishop) r = sub(8, nbrD);
    else if (p == Queen)  r = sub(8, BrdIdx::nbr4) + sub(8, nbrD);
    else if (p == King)   r = sub(1, BrdIdx::nbr8);
    else if (p == Knight) r = sub(1, nbrKn);
    ref.at(bi) = 1;
    return r + 1;
  };

  using pinfo_t = pair<BrdIdx, ll>;
  vector<pinfo_t> opiece;
  auto oput = [&](ll p) -> void {
    BrdIdx bi;
    while (true) {
      ll r = randrange(0, 8);
      ll c = randrange(0, 8);
      bi = BrdIdx(r, c);
      if (prepare.at(bi) == 0) break;
    }
    effect(bi, p, prepare);
    brd.at(bi) = p;
    opiece.emplace_back(bi, p);
  };
  oput(Queen);
  oput(Rook);
  oput(Bishop);
  oput(Knight);

  for (ll i = 0; i < 4; i++) {
    auto [bi, p] = opiece[i];
    cout << bi << " " << pname[p] << endl;
  }
  double time1 = get_time_sec();

  vector<ll> cand = {Rook, Bishop, Knight, King};
  vector<pinfo_t> stack(4);
  auto check = [&]() -> bool {
    Board<int> ref(8, 8, 0);
    ll cnt = 0;
    for (auto [bi, p] : opiece) cnt += effect(bi, p, ref);
    for (auto [bi, p] : stack)  cnt += effect(bi, p, ref);
    DLOGK(stack, cnt);
    return cnt == 64;
  };
  auto dfs = [&](auto rF, ll lev) -> void {
    if (lev == 4) {
      if (check()) {
        cout << "--\n";
        for (ll i = 0; i < 4; i++) {
          auto [bi, p] = stack[i];
          cout << bi << " " << pname[p] << "\n";
        }
        throw complete();
      }
      return;
    }
    for (ll r = 0; r < 8; r++) {
      for (ll c = 0; c < 8; c++) {
        BrdIdx bb(r, c);
        if (brd.at(bb) == 0) {
          brd.at(bb) = cand[lev];
          stack[lev] = pinfo_t(bb, cand[lev]);
          rF(rF, lev + 1);
          brd.at(bb) = 0;
        }
      }
    }
  };
  try {
    dfs(dfs, 0);
    cout << "No solution found\n";
  }catch(complete& e) {
  }
  double time2 = get_time_sec();
  cout << "time: " << time2 - time1 << " sec" << endl;

  return 0;
}

