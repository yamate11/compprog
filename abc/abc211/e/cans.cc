int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, K; cin >> N >> K;
  using ull = unsigned long long;
  auto enc = [&](ll i, ll j) -> ll { return i * N + j; };

  ull brd = 0;
  for (ll i = 0; i < N; i++) {
    string s; cin >> s;
    for (ll j = 0; j < N; j++) {
      if (s[j] == '#') brd |= (1ULL << enc(i, j));
    }
  }
  auto enable = [&](ll i, ll j) -> ll {
    return (brd & (1ULL << enc(i, j))) == 0;
  };

#ifdef DEBUG
  auto df = [&](set<ull> xs) -> void {
    for (ull x : xs) {
      for (ll i = 0; i < N; i++) {
        for (ll j = 0; j < N; j++) {
          if ((x >> enc(i, j)) & 1) {
            cerr << 'X';
          }else {
            cerr << '.';
          }
        }
        cerr << endl;
      }
      cerr<< endl;
    }
  };
#else
  auto df = [&](set<ull> xs) -> void {};
#endif

  using Pair = pair<ll, ll>;
  vector<Pair> nbr({{0, 1}, {1, 0}, {0, -1}, {-1, 0}});

  set<ull> cur;
  for (ll i = 0; i < N; i++) {
    for (ll j = 0; j < N; j++) {
      if (enable(i, j)) cur.insert(1ULL << enc(i, j));
    }
  }
  df(cur);
  for (ll k = 2; k <= K; k++) {
    DLOGK(k);
    set<ull> prev = move(cur);
    cur = set<ull>();
    for (ull x : prev) {
      for (ll i = 0; i < N; i++) {
        for (ll j = 0; j < N; j++) {
          if (! enable(i, j)) continue;
          if (! (x & (1ULL << enc(i, j)))) continue;
          for (auto [ii, jj] : nbr) {
            ll i0 = i + ii;
            ll j0 = j + jj;
            if (i0 < 0 || i0 >= N) continue;
            if (j0 < 0 || j0 >= N) continue;
            if (! enable(i0, j0)) continue;
            ull y = x | (1ULL << enc(i0, j0));
            if (x == y) continue;
            cur.insert(y);
          }
        }
      }
    }
    df(cur);
  }
  cout << cur.size() << endl;

  return 0;
}

