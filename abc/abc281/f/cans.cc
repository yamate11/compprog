int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N; cin >> N;
  // @InpVec(N, A) [apHwV1tr]
  auto A = vector(N, ll());
  for (int i = 0; i < N; i++) { ll v; cin >> v; A[i] = v; }
  // @End [apHwV1tr]
  auto f = [&](auto rF, ll k, auto& vec) -> ll {
    ll sz = SIZE(vec);
    if (sz == 1) return 0;
    if (k == 0) {
      if (FORALL(i, 1, sz, vec[0] == vec[i])) return 0;
      else return 1;
    }
    ll mask = (1LL << k) - 1;
    vector<ll> AA, BB;
    for (ll v : vec) {
      if (v >> k & 1) BB.push_back(v & mask);
      else            AA.push_back(v & mask);
    }
    if (AA.empty()) { return rF(rF, k - 1, BB); }
    if (BB.empty()) { return rF(rF, k - 1, AA); }
    ll aa = rF(rF, k - 1, AA);
    ll bb = rF(rF, k - 1, BB);
    return (1LL << k) + min(aa, bb);
  };
  cout << f(f, 30, A) << endl;

  return 0;
}

