int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, Q; cin >> N >> Q;
  
  vector<ll> xa, ya;
  vector<ll> xdep(N+1), ywid(N+1);

  ll newW = 0;


  ll curX = N;
  ll curY = N;
  for (ll q = 0; q < Q; q++) {
    DLOG("* q=", q);
    ll t; cin >> t;
    if (t == 1) {
      ll x; cin >> x;
      ll nxa = xa.size();
      auto checkX = [&](ll i) -> bool {
	return xa[i] < x;
      };
      ll xidx = binsearch<ll>(checkX, nxa, -1);
      DLOG("xidx=", xidx);
      if (xidx == nxa) {
	xa.push_back(x);
	xdep[nxa] = curY - 2;
	curX = x;
	newW += xdep[nxa];
      }else {
	newW += xdep[xidx];
      }
      DLOG("x q=", q, "newW=", newW);
    }else if (t == 2) {
      ll y; cin >> y;
      ll nya = ya.size();
      auto checkY = [&](ll i) -> bool {
	return ya[i] < y;
      };
      ll yidx = binsearch<ll>(checkY, nya, -1);
      if (yidx == nya) {
	ya.push_back(y);
	ywid[nya] = curX - 2;
	curY = y;
	newW += ywid[nya];
      }else {
	newW += ywid[yidx];
      }
      DLOG("y q=", q, "newW=", newW);
    }else assert(0);
  }
  cout << (N - 2) * (N - 2) - newW << endl;

  return 0;
}

