int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N; cin >> N;
  vector<ll> C(N);
  for (ll i = 0; i < N; i++) cin >> C[i];
  vector<vector<ll>> nbr(N);
  for (ll i = 0; i < N - 1; i++) {
    ll a, b; cin >> a >> b; a--; b--;
    nbr[a].push_back(b);
    nbr[b].push_back(a);
  }
  using sta = map<ll, ll>;
  using stb = pair<sta, ll>;
  vector<ll> excl(N + 1);
  auto dfs = [&](auto rF, ll nd, ll pt) -> stb {
    ll c0 = C[nd];
    bool first = true;
    sta mp0;
    ll num0 = 1;
    for (ll m : nbr[nd]) {
      if (m == pt) continue;
      auto [mp1, num1] = rF(rF, m, nd);
      auto it = mp1.find(c0);
      ll t = it == mp1.end() ? num1 : num1 - it->second;
      DLOG("  adding excl, nd=", nd, "c0=", c0, "t=", t);
      excl[c0] += t * (t + 1) / 2;
      if (first) {
	first = false;
	mp0 = move(mp1);
	num0 += num1;
      }else {
	num0 += num1;
	if (mp0.size() < mp1.size()) swap(mp0, mp1);
	for (auto [col, x] : mp1) mp0[col] += x;
      }
      DLOG("  updated mp0; nd=", nd, "mp0=", mp0);
    }
    mp0[c0] = num0;
    DLOG("nd=", nd, "num0=", num0, "mp0=", mp0, "excl=", excl);
    return stb(move(mp0), num0);
  };
  auto [mp, dummy] = dfs(dfs, 0, -1);
  DLOG("final mp=", mp);
  for (ll i = 1; i <= N; i++) {
    auto it = mp.find(i);
    ll x = it == mp.end() ? N : N - it->second;
    cout << N * (N + 1) / 2 - excl[i] - (x * (x + 1) / 2) << "\n";
  }

  return 0;
}

