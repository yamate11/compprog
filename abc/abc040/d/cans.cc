int main(int argc, char *argv[]) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  using Tuple = tuple<ll, ll, ll>;
  ll N, M; cin >> N >> M;
  vector<Tuple> info;
  for (ll i = 0; i < M; i++) {
    ll a, b, y; cin >> a >> b >> y; a--; b--;
    info.emplace_back(-y, a, b);
  }
  ll Q; cin >> Q;
  for (ll i = 0; i < Q; i++) {
    ll v, w; cin >> v >> w; v--;
    info.emplace_back(-w, -i-1, v);
  }
  sort(info.begin(), info.end());
  vector<ll> ans(Q);
  UnionFind uf(N);
  for (Tuple t : info) {
    DLOG("t", get<0>(t), get<1>(t), get<2>(t));
    // ll year = -get<0>(t);
    if (get<1>(t) < 0) {
      ll i = -get<1>(t) - 1;
      ll v = get<2>(t);
      ans.at(i) = uf.groupSize(v);
    }else {
      ll a = get<1>(t);
      ll b = get<2>(t);
      uf.merge(a, b);
    }
  }
  for (ll i = 0; i < Q; i++) {
    cout << ans.at(i) << endl;
  }
  return 0;
}

