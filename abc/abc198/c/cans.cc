  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll R, X, Y; cin >> R >> X >> Y;
  ll d2 = X*X + Y*Y;
  ll lim = divCeil(d2, R*R);
  auto check = [&](ll n) -> bool { return n * n >= lim; };
  ll ans = binsearch_i<ll>(check, 1e6, 0);
  if (ans == 1) {
    if (R * R != d2) ans = 2;
  }
  cout << ans << endl;

