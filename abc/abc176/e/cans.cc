int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll H, W, M; cin >> H >> W >> M;
  vector<ll> row(H + 1), col(W + 1);
  vector<ll> recH(M), recW(M);
  for (ll i = 0; i < M; i++) {
    ll h, w; cin >> h >> w;
    recH[i] = h;
    recW[i] = w;
    row[h]++;
    col[w]++;
  }
  DLOG("row", row);
  DLOG("col", col);
  ll hmax = 0;
  ll wmax = 0;
  for (ll h = 1; h <= H; h++) hmax = max(hmax, row[h]);
  for (ll w = 1; w <= W; w++) wmax = max(wmax, col[w]);
  vector<ll> vec_h(H + 1);
  for (ll i = 0; i < M; i++) {
    ll h = recH[i];
    ll w = recW[i];
    if (row[h] == hmax && col[w] == wmax) vec_h[h]++;
  }
  DLOG("vec_h", vec_h);
  ll cnt = 0;
  for (ll w = 1; w <= W; w++) if (col[w] == wmax) cnt++;
  DLOG("cnt", cnt);
  for (ll h = 1; h <= H; h++) {
    if (row[h] != hmax) continue;
    if (vec_h[h] < cnt) {
      cout << hmax + wmax << endl;
      return 0;
    }
  }
  cout << hmax + wmax - 1 << endl;

  return 0;
}

