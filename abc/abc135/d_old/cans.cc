constexpr ll prime = 1e9 + 7;

// g = eGCD(a, b, s, t)  --->  sa + tb = g
constexpr ll eGCD(ll a, ll b, ll& s, ll& t) {
  if (a == 0) {
    s = 0;
    t = 1;
    return b;
  }
  ll u = 0;
  ll g = eGCD(b % a, a, t, u);
  s = u - (b / a) * t;
  return g;
}

constexpr ll gcd(ll a, ll b) {
  ll dummy1 = 0;
  ll dummy2 = 0;
  return eGCD(a, b, dummy1, dummy2);
}

template<ll MOD>
struct Fp {
  ll val;

  Fp(ll t = 0) :
    val(t < 0 ? MOD + t % MOD : t >= MOD ? t % MOD : t) {}

  Fp& operator +=(const Fp& t) {
    val += t.val;
    if (val >= MOD) val -= MOD;
    return *this;
  }

  Fp& operator -=(const Fp& t) {
    val -= t.val;
    if (val < 0) val += MOD;
    return *this;
  }

  Fp& operator *=(const Fp& t) {
    val = (val * t.val) % MOD;
    return *this;
  }

  Fp inv() const {
    if (val == 0) {
      cerr << "inv() is called for zero." << endl;
      exit(1);
    }
    ll u = 0;
    ll v = 0;
    eGCD(val, MOD, u, v);
    return Fp(u);
  }

  Fp& operator /=(const Fp& t) {
    return (*this) *= t.inv();
  }

  Fp operator +(const Fp& t) const { return Fp(val) += t; }
  Fp operator -(const Fp& t) const { return Fp(val) -= t; }
  Fp operator *(const Fp& t) const { return Fp(val) *= t; }
  Fp operator /(const Fp& t) const { return Fp(val) /= t; }
  Fp operator -() const { return Fp(-val); }

  bool operator ==(const Fp& t) const { return val == t.val; }
  bool operator !=(const Fp& t) const { return val != t.val; }
  
  operator ll() const { return val; }

};





template <typename T>
using vector2 = vector<vector<T>>;
template <typename T>
vector2<T> init2(int n1, int n2) {
  return vector2<T>(n1, vector<T>(n2));
}



int main(int argc, char *argv[]) {
#if defined(DEBUG)
  // GDB on Cygwin ignores redirection at run command.
  if (argc == 2) dup2(open(argv[1], 0), 0);
#else
  // For performance.  We should not use C-style stdio functions
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
#endif
  cout << setprecision(20);

  string S;
  cin >> S;
  int len = S.size();
  vector2<Fp<prime>> dp = init2<Fp<prime>>(len + 1, 13);
  dp.at(0).at(0) = 1;
  Fp<13> power = 1;
  for (int i = 0; i < len; i++) {
    char c = S.at(len-1-i);
    for (int j = 0; j < 13; j++) {
      if (c == '?') {
	for (Fp<13> x = 0; x != Fp<13>(10); x += 1) {
	  Fp<13> jj = Fp<13>(j) + (power * x);
	  dp.at(i+1).at(jj) += dp.at(i).at(j);
	}
      }else {
	Fp<13> x = power * Fp<13>(c - '0');
	dp.at(i+1).at(Fp<13>(j) + x) = dp.at(i).at(j);
      }
    }
    power *= 10;
  }
  cout << dp.at(len).at(5) << endl;


  return 0;
}

