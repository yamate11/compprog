int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, M; cin >> N >> M;
  const ll lim = 1e10;
  vector<ll> X({-lim, lim, 0});
  vector<ll> Y({-lim, lim, 0});
  vector<ll> A(N), B(N), C(N);
  for (ll i = 0; i < N; i++) {
    cin >> A[i] >> B[i] >> C[i];
    X.push_back(A[i]);
    X.push_back(B[i]);
    Y.push_back(C[i]);
  }
  vector<ll> D(M), E(M), F(M);
  for (ll i = 0; i < M; i++) {
    cin >> D[i] >> E[i] >> F[i];
    X.push_back(D[i]);
    Y.push_back(E[i]);
    Y.push_back(F[i]);
  }
  sort(X.begin(), X.end());
  sort(Y.begin(), Y.end());
  X.erase(unique(X.begin(), X.end()), X.end());
  Y.erase(unique(Y.begin(), Y.end()), Y.end());
  ll szX = X.size();
  ll szY = Y.size();
  DLOGK(szX, szY);
  map<ll, ll> revX, revY;
  for (ll i = 0; i < szX; i++) revX[X[i]] = i;
  for (ll i = 0; i < szY; i++) revY[Y[i]] = i;

  Board<char> brd(2*szX, 2*szY, '.');
  Board<uint8_t> visited(2*szX, 2*szY, 0);
  for (ll i = 0; i < N; i++) {
    for (ll j = 2*revX[A[i]]; j < 2*revX[B[i]]; j++) {
      brd.at(j, 2*revY[C[i]]) = '#';
    }
  }
  for (ll i = 0; i < M; i++) {
    for (ll j = 2*revY[E[i]]; j < 2*revY[F[i]]; j++) {
      brd.at(2*revX[D[i]], j) = '#';
    }
  }
  for (ll i = 0; i < 2*szX; i++) brd.at(i, 0) = brd.at(i, 2*szY-1) = 'B';
  for (ll i = 0; i < 2*szY; i++) brd.at(0, i) = brd.at(2*szX-1, i) = 'B';

  ll ans = 0;
  queue<BrdIdx> que;
  que.emplace(2*revX[0], 2*revY[0]);
  while (!que.empty()) {
    BrdIdx bi = que.front(); que.pop();
    for (const BrdIdx& bd : BrdIdx::nbr4) {
      BrdIdx cur = bi + bd;
      if (brd.at(cur) == 'B') {
        cout << "INF\n";
        return 0;
      }
      if (brd.at(cur) == '#' || visited.at(cur)) continue;
      visited.at(cur) = 1;
      if (cur.r % 2 == 1 && cur.c % 2 == 1) {
        ans += (X[cur.r/2 + 1] - X[cur.r/2]) * (Y[cur.c/2 + 1] - Y[cur.c/2]);
      }
      que.push(move(cur));
    }
  }
  cout << ans << endl;
  

  return 0;
}

