struct MyExc : exception {};


int main(int argc, char *argv[]) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, M, P; cin >> N >> M >> P;
  vector<ll> A(M), B(M), C(M);
  vector<vector<ll>> nbr(N), rev(N);
  vector<bool> reachN(N), reachR(N);
  for (ll i = 0; i < M; i++) {
    ll a, b, c; cin >> a >> b >> c; a--; b--;
    A.at(i) = a;
    B.at(i) = b;
    C.at(i) = c - P;
    nbr.at(a).push_back(b);
    rev.at(b).push_back(a);
  }
  auto dfs = [&](const auto& rF, const auto& dir, auto& rch, ll i) {
    if (rch.at(i)) return;
    rch.at(i) = true;
    for (ll x : dir.at(i)) rF(rF, dir, rch, x);
  };
  auto eff = [&](ll i) -> bool { return reachN.at(i) && reachR.at(i); };
  dfs(dfs, nbr, reachN, 0);
  dfs(dfs, rev, reachR, N-1);

  DLOG("reachN", reachN);
  DLOG("reachR", reachR);

  vector<optional<ll>> vec(N);
  vec.at(0) = 0;
  for (ll q = 0; q < N; q++) {
    bool changed = false;
    for (ll i = 0; i < M; i++) {
      ll a = A.at(i);
      ll b = B.at(i);
      auto& va = vec.at(a);
      auto& vb = vec.at(b);
      if (!eff(a) || !eff(b) || !va.has_value()) continue;
      ll x = va.value() + C.at(i);
      if (!vb.has_value() || x > vb.value()) {
	vb = x;
	changed = true;
      }
    }
    if (!changed) {
      cout << max(0LL, vec.at(N-1).value()) << endl;
      return 0;
    }
  }
  cout << -1 << endl;

  return 0;
}

