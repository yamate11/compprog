int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  auto solve = [&]() -> ll {
    IPoint T, S, G;
    cin >> T >> S >> G;
    // DLOGK(T, S, G);

    ll fin_cost = abs(G.x - S.x) + abs(G.y - S.y);

    if (S.x == G.x) {
      T = T.rotate();
      S = S.rotate();
      G = G.rotate();
    }

    if (S.y == G.y) {
      if (G.x < S.x) {
        T = -T;
        S = -S;
        G = -G;
      }
      if (T.y == S.y and S.x < T.x) {
        ll pre_cost = T.x - (S.x - 1) + 2;
        return pre_cost + fin_cost;
      }else {
        ll pre_cost = abs(T.x - (S.x - 1)) + abs(T.y - S.y);
        return pre_cost + fin_cost;
      }
    }
      
    while (true) {
      if (S.x < G.x and S.y < G.y) break;
      T = T.rotate();
      S = S.rotate();
      G = G.rotate();
    }
    if (S.x <= T.x and S.y <= T.y) {
      ll pre_cost = T.x - S.x + T.y - S.y + 1 + 2;
      return pre_cost + fin_cost;
    }
    if (T.x <= S.x - 1 and T.y <= S.y - 1) {
      ll pre_cost = (S.x - 1 - T.x) + (S.y - 1 - T.y) + 1 + 2;
      return pre_cost + fin_cost;
    }
    if (S.x <= T.x and T.y <= S.y - 1) {
      ll pre_cost = (T.x - S.x) + (S.y - 1 - T.y) + 2;
      return pre_cost + fin_cost;
    }
    if (T.x <= S.x - 1 and S.y <= T.y) {
      ll pre_cost = (S.x - 1 - T.x) + (T.y - S.y) + 2;
      return pre_cost + fin_cost;
    }
    assert(0);
  };

  cout << solve() << endl;

  return 0;
}

