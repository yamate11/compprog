  using T = typename AO::ult;
  T v;  // value
  BwAO(const T& v_) : v(v_) {}
  BwAO(T&& v_) : v(move(v_)) {}
  BwAO& operator =(const T& v_) { v = v_; return *this; }
  BwAO& operator =(T&& v_) { v = move(v_); return *this; }
  operator T() const { return v; }
  bool operator ==(const BwAO& o) { return v == o.v; }
  bool operator !=(const BwAO& o) { return v != o.v; }
