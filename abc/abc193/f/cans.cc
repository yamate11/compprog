int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N; cin >> N;
  Board brd(cin, N, N, '#');
  
  ll nTrue = N*N;
  ll nFalse = N*N + 1;
  Dinic d(N*N + 2, nTrue, nFalse);

  ll big = 1e9;

  for (ll r = 0; r < N; r++) {
    for (ll c = 0; c < N; c++) {
      BrdIdx bi(r, c);
      ll nd = r * N + c;
      char ch = brd.get(bi);
      if (ch == 'B') {
        if ((r + c) % 2 == 0) {
          DLOGK(nTrue, nd, big);
          d.add_edge(nTrue, nd, big);
        }else {
          DLOGK(nd, nFalse, big);
          d.add_edge(nd, nFalse, big);
        }
      }
      if (ch == 'W') {
        if ((r + c) % 2 == 1) {
          DLOGK(nTrue, nd, big);
          d.add_edge(nTrue, nd, big);
        }else {
          DLOGK(nd, nFalse, big);
          d.add_edge(nd, nFalse, big);
        }
      }

      for (BrdIdx bd: BrdIdx::nbr4) {
        BrdIdx bz = bi + bd;
        ll nd2 = bz.r * N + bz.c;
        if (!brd.in(bz)) continue;
        DLOGK(nd, nd2, 1);
        // DLOGK(nd2, nd, 1);
        d.add_edge(nd, nd2, 1);
        // d.add_edge(nd2, nd, 1);
      }
    }
  }

  
  ll flow = d.run();
  DLOGK(flow);
  cout << (N - 1) * N * 2 - flow << endl;

  return 0;
}

