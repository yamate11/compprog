int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, M; cin >> N >> M;
  using sta = pair<ll, Ftwo>;
  vector<sta> AB;
  for (ll i = 0; i < N; i++) {
    ll a, b; cin >> a >> b;
    AB.emplace_back(a, b);
  }
  AB.emplace_back(0, 0);
  AB.emplace_back((ll)(1e10), 0);
  sort(AB.begin(), AB.end());
  vector<Ftwo> bs;
  for (ll i = 1; i < (ll)AB.size(); i++) {
    bs.push_back(AB[i-1].second + AB[i].second);
  }
  using stb = pair<ll, ll>;
  vector<stb> sw;
  for (ll i = 0; i < M; i++) {
    ll l, r; cin >> l >> r;
    ll idxL = lower_bound(AB.begin(), AB.end(), sta(l, 0)) - AB.begin() - 1;
    ll idxR = upper_bound(AB.begin(), AB.end(), sta(r, 1)) - AB.begin() - 1;
    sw.emplace_back(idxL, idxR);
  }
  DLOGK(sw);
  DLOGK(bs);
  auto optret = adjMatEq(sw, bs);
  if (optret.has_value()) {
    auto sol = optret.value();
    DLOGK(sol);
    vector<ll> ans;
    for (ll i = 0; i < M; i++) if (sol[i]) ans.push_back(i);
    cout << ans.size() << endl;
    for (ll i : ans) cout << i + 1 << " ";
    cout << endl;
  }else {
    cout << -1 << endl;
  }

  return 0;
}

