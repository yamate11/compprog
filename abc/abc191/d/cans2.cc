int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  long double dbX, dbY, dbR; cin >> dbX >> dbY >> dbR;
  ll X = llround(dbX * 1e4);
  ll Y = llround(dbY * 1e4);
  ll R = llround(dbR * 1e4);
  ll L4 = 1e4;
  ll cnt = 0;
  for (ll x = L4 * divCeil(X - R, L4); x <= L4 * divFloor(X + R, L4); x += L4) {
    ll xx = (x - X) * (x - X);
    long double yy = sqrt((long double)(R * R - xx));
    ll ymax = llround(floor((Y + yy) / L4));
    ll ymin = llround(ceil( (Y - yy) / L4));
    cnt += ymax - ymin + 1;
  }
  cout << cnt << endl;

  return 0;
}

