#include <bits/stdc++.h>
#include <cassert>
typedef long long int ll;
using namespace std;

int main(int argc, char *argv[]) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  int x = 8;
  cout << ~x << endl;
  unsigned int y = 8;
  unsigned int z = ~y;
  long long w = ~y;
  cout << z << endl;
  cout << w << endl;
  

}
