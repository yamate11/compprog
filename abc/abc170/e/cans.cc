int main(int argc, char *argv[]) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  using Pair = pair<ll, ll>;

#if DEBUG
  ll lim = 4;
#else
  ll lim = 2e5;
#endif
  ll N, Q; cin >> N >> Q;
  vector<ll> rate(N+1), pup2kg(N+1);
  vector<set<Pair>> kg(lim+1);
  set<Pair> smax;

  for (ll i = 1; i <= N; i++) {
    ll a, b; cin >> a >> b;
    rate.at(i) = a;
    pup2kg.at(i) = b;
    kg.at(b).emplace(a, i);
  }
  for (ll i = 1; i <= lim; i++) {
    auto s = kg.at(i);
    if (s.empty()) continue;
    smax.insert(*(s.rbegin()));
  }

  for (ll _q = 0; _q < Q; _q++) {
    ll pup, after; cin >> pup >> after;
    ll prev = pup2kg.at(pup);
    pup2kg.at(pup) = after;
    auto& prevset = kg.at(prev);
    Pair pupPair = make_pair(rate.at(pup), pup);
    smax.erase(*(prevset.rbegin()));
    prevset.erase(pupPair);
    if (!prevset.empty()) {
      smax.insert(*(prevset.rbegin()));
    }
    auto& afterset = kg.at(after);
    if (!afterset.empty()) {
      smax.erase(*(afterset.rbegin()));
    }
    afterset.insert(pupPair);
    smax.insert(*(afterset.rbegin()));
    cout << smax.begin()->first << "\n";
    DLOG("kg", kg);
    DLOG("smax", smax);
  }

  return 0;
}

