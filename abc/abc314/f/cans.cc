  auto Q = vector(N - 1, ll());
  for (int i = 0; i < N - 1; i++) {
    ll v1; cin >> v1; v1 -= 1; P[i] = v1;
    ll v2; cin >> v2; v2 -= 1; Q[i] = v2;
  }
