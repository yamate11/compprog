int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, P, Q, R, S; cin >> N >> P >> Q >> R >> S; P--; R--;
  // @InpVec(N, A) [TN3sVJ1R]
  auto A = vector(N, ll());
  for (int i = 0; i < N; i++) { ll v; cin >> v; A[i] = v; }
  // @End [TN3sVJ1R]
  REP(i, P, Q) {
    swap(A[i], A[i + R - P]);
  }
  REPOUT(i, 0, N, A[i], " ");

  return 0;
}

