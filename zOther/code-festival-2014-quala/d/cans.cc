int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll A; cin >> A;
  ll K; cin >> K;
  
  auto msd = [](ll x) -> pair<ll, ll> {
    auto sub = [](auto rF, ll z) -> pair<ll, ll> {
      if (z < 10) return {z, 1};
      auto [y, k] = rF(rF, z / 10);
      return {y, k * 10};
    };
    return sub(sub, x);
  };
    
  const ll big = 1e16;
  auto func = [&](auto rF, ll x, ll used) -> ll {
    auto d_find = [&](ll from, ll dir, ll tmp_used) -> ll {
      for (ll e = from; 0 <= e && e <= 9; e += dir) {
        if (__builtin_popcountll(tmp_used) < K || ((tmp_used >> e) & 1)) {
          return e;
        }
      }
      return -1;
    };
    auto same = [&](ll m, ll e) -> ll {
      ll ret = 0;
      for ( ; m > 0; m = m / 10) ret = ret * 10 + e;
      return ret;
    };

    DLOGKL("func", x, used);
    auto [d, m] = msd(x);
    DLOGK(x, d, m);
    // larger one
    ll dl = d_find(d + 1, 1, used);
    ll ans = big;
    if (dl >= 0) {
      ll y = d_find(0, 1, used | (1LL << dl));
      assert(y >= 0);
      ll lg_cand = (d + 1) * m + same(m / 10, y);
      ll lg_diff = lg_cand - x;
      DLOGK(x, lg_cand, lg_diff, y);
      ans = min(ans, lg_diff);
    }
    ll ds, tmp_used;
    if (d == 1 && x == A) {
      ds = 0;
      tmp_used = used;
    }else {
      ds = d_find(d - 1, -1, used);
      tmp_used = used | (1LL << ds);
    }
    if (ds >= 0) {
      ll y = d_find(9, -1, tmp_used);
      assert(y >= 0);
      ll sm_cand = (d - 1) * m + same(m / 10, y);
      ll sm_diff = x - sm_cand;
      DLOGK(x, sm_cand, sm_diff, y);
      ans = min(ans, sm_diff);
    }
    if (((used >> d) & 1) || __builtin_popcountll(used) < K) {
      ll new_x = x - d * m;
      ll new_used = used | (1LL << d);
      ll v = rF(rF, new_x, new_used);
      DLOGK(x, v);
      ans = min(ans, v);
    }
    DLOGK(x, ans);
    return ans;
  };
  ll ans = func(func, A, 0);
  cout << ans << endl;

  return 0;
}

