int main(int argc, char *argv[]) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll T; cin >> T;

  auto solve = [&]() -> ll {
    ll N; cin >> N;
    using StB = pair<ll, ll>;  // size, penalty
    vector<StB> infoL, infoR;
    ll tot = 0;

    auto func = [&](const auto& info) -> ll {
      using StA = ll;
      priority_queue<StA, vector<StA>, greater<StA>> pque;
      ll ret = 0;
      ll cap = 0;
      for (auto [sz, pen] : info) {
	pque.push(pen);
	DLOG("sz=", sz, "cap=", cap);
	if (sz < cap + 1) {
	  ll ppp = pque.top(); pque.pop();
	  ret += ppp;
	}else {
	  cap++;
	}
      }
      return ret;
    };

    for (ll i = 0; i < N; i++) {
      ll k, l, r; cin >> k >> l >> r;
      ll vmax = max(l, r);
      tot += vmax;
      if (l == r) continue;
      if (l > r) {
	infoL.emplace_back(k, l-r);
      }else {
	infoR.emplace_back(N-k, r-l);
      }
    }
    sort(infoL.begin(), infoL.end());
    sort(infoR.begin(), infoR.end());
    return tot - func(infoL) - func(infoR);
  };

  for (ll t = 0; t < T; t++) {
    cout << solve() << "\n";
  }
    

  return 0;
}

