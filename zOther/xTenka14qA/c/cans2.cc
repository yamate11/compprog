int main(int argc, char *argv[]) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, M; cin >> N >> M;
  vector<string> P(N);
  for (ll i = 0; i < N; i++) cin >> P.at(i);
  vector<vector<bool>> compat(N, vector<bool>(N, true));
  for (ll i = 0; i < N; i++) {
    for (ll j = i+1; j < N; j++) {
      for (ll k = 0; k < M; k++) {
	ll ci = P.at(i).at(k);
	ll cj = P.at(j).at(k);
	if (ci != '*' && cj != '*' && ci != cj) {
	  compat.at(i).at(j) = compat.at(j).at(i) = false;
	  break;
	}
      }
    }
  }
  DLOG(compat);

  ll size = 1LL << N;
  ll fullset = size - 1;
  vector<ll> result(size, -1);
  auto func = [&](const auto& rf, ll x) -> ll {
    ll& ret = result.at(x);
    if (ret == -1) {
      ll k = 63 - __builtin_clzll(x);
      ll b = 1LL << k;
      auto uniform = [&]() -> bool {
	if (x == b) return true;
	ll y = x ^ b;
	if (rf(rf, y) != 1) return false;
	for (ll m = k - 1; m >= 0; m--) {
	  if (!(y & (1LL << m))) continue;
	  if (!compat.at(k).at(m)) return false;
	}
	return true;
      };
      if (uniform()) ret = 1;
      else {
	ret = N+1;
	for (ll y = (x ^ b); y > 0; y = ((y - 1) & x)) {
	  ll t = rf(rf, y) + rf(rf, x ^ y);
	  if (ret > t) {
	    ret = t;
	    if (ret == 2) break;
	  }
	}
      }
    }
    return ret;
  };
  ll ans = func(func, fullset);
  cout << ans << endl;
  return 0;
}

