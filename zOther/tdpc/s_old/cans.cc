using Fp = FpA;

struct myexc : exception {};

int naive(istream& cin, ostream& cout) {
  ll H, W; cin >> H >> W;

  Board<int> brd(H, W, 0);
  ll M = H * W;
  ll cnt = 0;
  for (ll x = 0; x < (1LL << (M - 2)); x++) {
    ll y = (1LL << (M - 1)) | (x << 1) | 1;
    for (ll t = 0; t < M; t++) {
      ll r = t / W;
      ll c = t % W;
      brd.at(r, c) = (y >> t) & 1;
    }
    Board<int> visited(H, W, 0);
    auto dfs = [&](auto rF, BrdIdx bi) -> void {
      if (bi.r == H - 1 && bi.c == W - 1) throw myexc();
      for (auto& d : BrdIdx::nbr4) {
        auto bn = bi + d;
        if (brd.at(bn) == 0) continue;
        if (visited.at(bn)) continue;
        visited.at(bn) = 1;
        rF(rF, bn);
      }
    };
    try {
      dfs(dfs, BrdIdx(0, 0));
    }catch (myexc& e) {
      cnt++;
    }
  }
  cout << cnt << endl;
  return 0;
}
int body(istream& cin, ostream& cout) {

  ll H, W; cin >> H >> W;

  auto enc = [&](const vector<ll>& vec) -> ll {
    ll ret = 0;
    for (ll i = H - 1; i >= 0; i--) ret = 4 * ret + vec[i];
    return ret;
  };
  auto dec = [&](ll e) -> vector<ll> {
    vector<ll> vec(H);
    for (ll i = 0; i < H; i++) {
      vec[i] = e % 4;
      e /= 4;
    }
    return vec;
  };

  vector<vector<pair<ll, ll>>> blocks(1LL << H);
  vector<vector<ll>> whites(1LL << H);

  for (ll x = 0; x < (1LL << H); x++) {
    for (ll i = 0; i < H; i++) if ((x >> i) & 1) whites[x].push_back(i);
    ll p = 0;
    while (true) {
      while (p < H && ((x >> p) & 1)) p++;
      if (p >= H) break;
      ll q = p + 1;
      while (q < H && (! ((x >> q) & 1))) q++;
      blocks[x].emplace_back(p, q);
      p = q + 1;
    }
  }


  ll pow4 = 1LL << (2 * H);
  vector<vector<ll>> nxt(pow4);
  auto getNxt = [&](ll e) -> const vector<ll>& {
    vector<ll>& v = nxt[e];
    if (v.size() == 0) {
      auto x = dec(e);
      for (ll z = 0; z < (1LL << H); z++) {
        vector<ll> f(H, -1);
        vector<bool> w(4);
        vector<pair<ll, ll>> notyet;
        for (auto [p, q] : blocks[z]) {
          ll t = x[p];
          for (ll j = p + 1; j < q; j++) if (x[j] < t) t = x[j];
          if (t == 3) notyet.emplace_back(p, q);
          else {
            w[t] = true;
            for (ll j = p; j < q; j++) f[j] = t;
          }
        }
        if (! w[0]) continue;
        ll s = 0;
        for (auto [p, q] : notyet) {
          s++;
          while (w[s]) s++;
          assert(s == 1 || s == 2);
          for (ll j = p; j < q; j++) f[j] = s;
        }
        for (ll j : whites[z]) f[j] = 3;
        v.push_back(enc(f));
      }
      DLOGK(e, v);
    }
    return v;
  };

  vector<ll> x_start(H, 3), x_end(H, 3);
  x_start[0] = 0;
  x_end[H-1] = 0;
  ll e_start = enc(x_start);
  ll e_end = enc(x_end);

  map<ll, Fp> cur;
  cur[e_start] = Fp(1);
  for (ll i = 0; i <= W; i++) {
    auto prev = move(cur);
    cur = map<ll, Fp>();
    for (auto [e, n] : prev) {
      for (ll f : getNxt(e)) cur[f] += n;
    }
  }
  cout << cur[e_end] << endl;

  return 0;
}

