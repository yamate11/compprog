using Fp = FpA;

int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll H, W; cin >> H >> W;

  auto b_on = [](ll x, ll i) -> bool { return ((x >> i) & 1) != 0; };
  auto q_val = [](ll y, ll i) -> ll { return (y >> (2*i)) & 3; };
  auto q_set = [](ll& y, ll i, ll t) -> void {
    y = ((y & ~(3LL << (2*i))) | (t << (2*i)));
  };
  auto memo = vector(1LL << (2*H), vector(1LL << H, -1LL));
  auto func = [&](ll x, ll y) -> ll {
    ll& ret = memo[x][y];
    if (ret < 0) {
      UnionFind uf(4 + H);
      for (ll i = 0; i+1 < H; i++) {
        if (b_on(y, i) && b_on(y, i+1)) uf.merge(4+i, 4+i+1);
      }
      for (ll i = 0; i < H; i++) {
        ll t = q_val(x, i);
        if (b_on(y, i) && t != 0) uf.merge(4+i, t);
      }
      ret = 0;
      bool reachable = false;
      ll rep_two = -1;
      for (ll i = 0; i < H; i++) {
        if (! b_on(y, i)) continue;
        if (uf.leader(1) == uf.leader(4+i)) {
          q_set(ret, i, 1);
          reachable = true;
        }else if (rep_two < 0) {
          q_set(ret, i, 2);
          rep_two = i;
        }else if (uf.leader(4+i) == uf.leader(4+rep_two)) {
          q_set(ret, i, 2);
        }else {
          q_set(ret, i, 3);
        }
      }
      if (!reachable) ret = 0;
    }
    return ret;
  };

  vector cur_init = vector<Fp>(1LL << (2*H), Fp(0));
  auto cur = cur_init;
  cur[1] = Fp(1);
  for (ll i = 0; i < W; i++) {
    auto prev(move(cur));
    cur = cur_init;
    for (ll x = 0; x < (1LL << (2*H)); x++) {
      if (prev[x] == Fp(0)) continue;
      for (ll y = 0; y < (1LL << H); y++) {
        ll z = func(x, y);
        if (z > 0) cur[z] += prev[x];
      }
    }
  }
  Fp ans = 0;
  for (ll x = 0; x < (1LL << (2*H)); x++) {
    if (cur[x] == Fp(0)) continue;
    if (q_val(x, H-1) == 1) ans += cur[x];
  }
  cout << ans << endl;

  return 0;
}

