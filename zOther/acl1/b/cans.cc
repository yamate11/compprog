int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N; cin >> N;
  ll N2 = N * 2;
  auto ds = prfac(N2);
  ll m = ds.size();
  vector<ll> gs(m);
  for (ll i = 0; i < m; i++) {
    auto [p, r] = ds[i];
    gs[i] = power<ll>(p, r);
  }
  DLOG("gs=", gs);
  ll k = N2;
  for (ll x = 0; x < (1 << m); x++) {
    ll g1 = 1, g2 = 1;
    for (ll i = 0; i < m; i++) {
      if ((x >> i) & 1) g1 *= gs[i];
      else             g2 *= gs[i];
    }
    ll s = 0, t = 0; eGCD(g1, g2, s, t);
    ll pp = divFloor(-s * g1, N2);
    ll z = -s * g1 - N2 * pp;
    DLOG("g1=", g1, "g2=", g2, "x=", x, "s=", s, "t=", t, "z=", z);
    if (z > 0) k = min(k, z);
  }
  cout << k << endl;


  return 0;
}

