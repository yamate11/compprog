int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll sz = (1LL << 18);
  vector<bool> chk(sz);
  auto icheck = [&](ll x) -> bool {
    for (ll i = 0; i < 6; i++) {
      ll c = ((x >> i) & 1) + ((x >> (12 + i)) & 1);
      if (i-1 >= 0) c += ((x >> (6 + (i-1))) & 1);
      if (i+1 < 6)  c += ((x >> (6 + (i+1))) & 1);
      ll v = ((x >> (6 + i)) & 1);
      if (c < 2 && v == 1) return false;
      if (c > 2 && v == 0) return false;
    }
    return true;
  };
  for (ll x = 0; x < (1LL << 18); x++) chk[x] = icheck(x);

  vector<string> S(18);
  for (ll i = 0; i < 18; i++) cin >> S[i];

  using tbl_t = vector<ll>;
  const ll tbl_sz = 1LL << 12;
  tbl_t cur(tbl_sz);
  cur[0] = 1;
  for (ll row = 0; row < 18; row++) {
    tbl_t prev = move(cur);
    cur = tbl_t(tbl_sz);
    ll mask = 0;
    ll fix = 0;
    for (ll i = 0; i < 6; i++) {
      if (S[row][i] == '?') mask |= (1LL << i);
      else if (S[row][i] == '1') fix |= (1LL << i);
    }
    for (ll z = 0; z < tbl_sz; z++) {
      if (prev[z] == 0) continue;
      ll x = mask;
      while (true) {
	ll y = x | fix;
	ll t = ((z << 6) | y);
	if (chk[t]) {
	  cur[t & (tbl_sz - 1)] += prev[z];
	}
	if (x == 0) break;
	x = (x - 1) & mask;
      }
    }
    ll xx = 0;
    for (ll i = 0; i < tbl_sz; i++) xx += cur[i];
    DLOGK(row, xx);
  }
  ll ans = 0;
  for (ll z = 0; z < tbl_sz; z++) {
    if (chk[z << 6]) ans += cur[z];
  }
  cout << ans << endl;

  return 0;
}

