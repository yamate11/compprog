int naive(istream& cin, ostream& cout) {
  ll N; cin >> N;
  vector<ll> A(N), U(N - 1), V(N - 1);
  for (ll i = 0; i < N; i++) {
    cin >> A[i];
  }
  for (ll i = 0; i < N-1; i++) {
    ll u, v; cin >> u >> v; u--; v--;
    U[i] = u;
    V[i] = v;
  }
  ll ans = N;
  for (ll x = 0; x < (1LL << (N-1)); x++) {
    UnionFind uf(N);
    ll cnt = 0;
    for (ll i = 0; i < N-1; i++) {
      if ((x >> i) & 1) {
        cnt++;
      }else {
        uf.merge(U[i], V[i]);
      }
    }
    vector<bool> comp(N);
    vector<ll> power(N);
    for (ll i = 0; i < N; i++) {
      if (A[i] < 0) comp[uf.leader(i)] = true;
      power[uf.leader(i)] += A[i];
    }
    bool succ = true;
    for (ll i = 0; i < N; i++) {
      if (i != uf.leader(i)) continue;
      if (comp[i] && power[i] >= 0) {
        succ = false;
        break;
      }
    }
    if (succ) ans = min(ans, cnt);
  }
  cout << ans << endl;
  return 0;
}
int body(istream& cin, ostream& cout) {
  ll N; cin >> N;
  vector<ll> A(N + 1);
  for (ll i = 1; i <= N; i++) cin >> A[i];
  vector<vector<ll>> nbr(N + 1);
  for (ll i = 1; i <= N-1; i++) {
    ll u, v; cin >> u >> v;
    nbr[u].push_back(v);
    nbr[v].push_back(u);
  }

  auto min_neg = [&](const auto& vec) -> ll {
    size_t i = 0;
    for ( ; i < vec.size(); i++) if (vec[i] < 0) return i;
    return -1;
  };
  auto dfs = [&](auto rF, ll nd, ll pt) -> pair<ll, vector<ll>> {
    ll nc = 0;
    auto vev = vector<ll>({A[nd]});
    auto updzero = [&](size_t i) -> void {
      if (i < vev.size()) updMin(vev[i], 0LL);
      else                vev.push_back(0);
    };
    for (ll cld : nbr[nd]) {
      if (cld == pt) continue;
      auto [c_nc, c_vev] = rF(rF, cld, nd);
      nc += c_nc;
      auto prev = move(vev);
      vev = vector<ll>(prev.size() + c_vev.size() - 1, LLONG_MAX);
      for (size_t i = 0; i < prev.size(); i++) {
        for (size_t j = 0; j < c_vev.size(); j++) {
          updMin(vev[i + j], prev[i] + c_vev[j]);
        }
      }
    }
    DLOGKL("  ", nc, vev);
    if (A[nd] < 0) {
      ll mn = min_neg(vev);
      assert(mn >= 0);
      nc = mn + 1;
    }else {
      updzero(nc + 1);
      ll mn = min_neg(vev);
      if (mn >= 0) {
        nc = min(nc, mn + 1);
      }
    }
    updzero(vev.size());
    DLOGK(nd, nc, vev);
    return make_pair(nc, move(vev));
  };
  auto [nc, vev] = dfs(dfs, 1, -1);
  ll mn = min_neg(vev);
  ll ans = mn < 0 ? nc : min(nc, mn);
  cout << ans << endl;

  return 0;
}

