int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, Q; cin >> N >> Q;
  using sta = tuple<ll, ll, ll>;
  vector<sta> L, R;
  const ll big = 1e15;
  for (ll i = 1; i <= N-1; i++) {
    ll l, r; cin >> l >> r;
    L.emplace_back(l, -1, i);
    R.emplace_back(r, -1, i);
  }
  L.emplace_back(big, -1, 0);
  L.emplace_back(big, -1, N);
  R.emplace_back(-big, -1, 0);
  R.emplace_back(-big, -1, N);
  for (ll i = 0; i < Q; i++) {
    ll a, b; cin >> a >> b;
    L.emplace_back(a, b, i);
    R.emplace_back(a, b, i);
  }
  sort(L.begin(), L.end(), [&](sta x, sta y) -> bool {
    auto [lx, dx, ix] = x;
    auto [ly, dy, iy] = y;
    if (lx != ly) return lx > ly;
    return dx > dy;
  });
  sort(R.begin(), R.end(), [&](sta x, sta y) -> bool {
    auto [lx, dx, ix] = x;
    auto [ly, dy, iy] = y;
    if (lx != ly) return lx < ly;
    return dx > dy;
  });
  DLOGK(L);
  DLOGK(R);
  vector<ll> limLow(Q, 1), limHi(Q, N);
  auto func = [&](vector<sta>& T) -> void {
    set<ll> bar;
    for (auto [l, d, i] : T) {
      DLOGK(bar, l, d, i);
      if (d == -1) bar.insert(i);
      else {
        auto it = bar.lower_bound(d);
        updMin(limHi[i], *it);
        it--;
        updMax(limLow[i], *it + 1);
      }
    }
  };
  func(L);
  func(R);
  for (ll i = 0; i < Q; i++) {
    cout << limHi[i] - limLow[i] + 1 << "\n";
  }

  return 0;
}

