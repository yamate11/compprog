int main(int argc, char *argv[]) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N; cin >> N;
  using Pair = pair<ll, ll>;
  map <ll, Pair> mp;
  ll big = LLONG_MAX;
  mp[0] = make_pair(0, big);
  mp[big] = make_pair(0, 0);
  for (ll _z = 0; _z < N; _z++) {
    ll s, c; cin >> s >> c;
    auto it = mp.upper_bound(s); it--;
    ll x = it->first;
    ll b = it->second.first;
    ll w = it->second.second;
    if (x + b < s) {
      mp[x] = make_pair(b, s - (x+b));
      w = x + w + b - s;
      b = 0;
      x = s;
      it++;
    }else {
      it = mp.erase(it);
    }
    while (w < c) {
      b += w;
      c -= w;
      b += it->second.first;
      w = it->second.second;
      it = mp.erase(it);
    }
    mp[x] = make_pair(b + c, w - c);
    DLOG(mp);
    cout << x + b + c - 1 << "\n";
  }

    

  return 0;
}

