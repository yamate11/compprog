int main(int argc, char *argv[]) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll size = 205;

  vector<BrdIdx> nbr({{1,1}, {0,1}, {-1,1}, {1,0}, {-1,0}, {0,-1}});

  ll N, X, Y; cin >> N >> X >> Y;
  Board<char> brd(size*2, size*2, 1);
  Board<char> visited(size*2, size*2, 0);
  for (ll i = 0; i < N; i++) {
    ll x, y; cin >> x >> y;
    x += size; y += size;
    brd.set(x, y, 0);
  }
  using Pair = pair<BrdIdx, ll>;
  queue<Pair> que;
  que.emplace(BrdIdx(size, size), 0);
  while (!que.empty()) {
    auto [bi, d] = que.front(); que.pop();
    if (visited.get(bi)) continue;
    visited.set(bi, 1);
    if (bi.r == X + size && bi.c == Y + size) {
      cout << d << endl;
      return 0;
    }
    for (auto bdiff : nbr) {
      BrdIdx bnew = bi + bdiff;
      if (brd.get(bnew) == 0) continue;
      if (!brd.in(bnew)) continue;
      if (visited.get(bnew)) continue;
      que.emplace(bnew, d + 1);
    }

  }
  cout << -1 << endl;

  return 0;
}

