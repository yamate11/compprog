using sta = pair<ll, ll>;
typedef tree<
  sta,
  null_type,
  less<sta>,
  rb_tree_tag,
  tree_order_statistics_node_update>
ordered_set;


int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, K, P; cin >> N >> K >> P;
  vector A(N, 0LL); cin >> A;
  vector<ordered_set> oss(K + 1);
  ll n2 = N / 2;
  for (ll x = 0; x < (1LL << n2); x++) {
    ll s = 0;
    ll p = 0;
    for (ll t = 0; t < n2; t++) {
      if ((x >> t) & 1) {
        p++;
        s += A[t];
      }
    }
    if (p <= K) oss[p].insert(sta(s, x));
  }
  ll m2 = N - n2;
  ll ans = 0;
  for (ll x = 0; x < (1LL << m2); x++) {
    ll s = 0;
    ll p = 0;
    for (ll t = 0; t < m2; t++) {
      if ((x >> t) & 1) {
        p++;
        s += A[n2 + t];
      }
    }
    if (0 <= K - p) {
      ll e = oss[K - p].order_of_key(sta(P - s + 1, -1));
      // DLOGK(s, e);
      ans += e;
    }
  }
  cout << ans << endl;

  return 0;
}

