int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll T, L, X, Y, Q; cin >> T >> L >> X >> Y >> Q;
  for (ll _q = 0; _q < Q; _q++) {
    ll e; cin >> e;
    double theta = 2.0 * M_PI * (0.75 - e / double(T));
    double r = 0.5 * L;
    double p0 = 0.0, p1 = r * cos(theta), p2 = r * (1.0 + sin(theta));
    double q0 = double(X), q1 = double(Y), q2 = 0.0;
    double u0 = q0 - p0, u1 = q1 - p1, u2 = q2 - p2;
    double v0 = 0.0, v1 = 0.0, v2 = -1.0;
    double prod = u0 * v0 + u1 * v1 + u2 * v2;
    double usize = sqrt(u0 * u0 + u1 * u1 + u2 * u2);
    double vsize = sqrt(v0 + v0 + v1 * v1 + v2 * v2);
    double z = prod / (usize * vsize);
    double arg = acos(z) / M_PI * 180.0;
    DLOGK(theta, p0, p1, p2);
    DLOGK(u0, u1, u2, v0, v1, v2, usize, vsize, z);
    cout << 90 - arg << "\n";
  }

  return 0;
}

