int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, L, K; cin >> N >> L >> K;
  vector<ll> A(N);
  for (ll i = 0; i < N; i++) cin >> A[i];
  auto check = [&](ll y) -> bool {
    ll cnt = 0;
    ll pos = 0;
    ll i = 0;
    for (; i < N; i++) {
      if (A[i] - pos >= y) {
        cnt++;
        pos = A[i];
      }
    }
    if (L - pos >= y) cnt++;
    return cnt >= K+1;
  };
  ll ans = binsearch_i<ll>(check, 1, L);
  cout << ans << endl;
  

  return 0;
}

