int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, K; cin >> N >> K;
  if (K % 2 == 1) {
    ll p = N / K;
    cout << p * p * p << endl;
    return 0;
  }else {
    ll k = K / 2;
    ll p = N / k;
    ll s = p / 2;
    ll t = p - s;
    cout << s * s * s + t * t * t << endl;
    return 0;
  }

  return 0;
}

