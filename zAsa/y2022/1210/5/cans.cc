int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, M; cin >> N >> M;
  // @InpMVec(M, ((A, dec=1), (B, dec=1))) [wPqX3g0y]
  auto A = vector(M, ll());
  auto B = vector(M, ll());
  for (int i = 0; i < M; i++) {
    ll v1; cin >> v1; v1 -= 1; A[i] = v1;
    ll v2; cin >> v2; v2 -= 1; B[i] = v2;
  }
  // @End [wPqX3g0y]
  vector<ll> ord(M);
  iota(ALL(ord), 0);
  sort(ALL(ord), [&](ll i, ll j) -> bool { return B[i] < B[j]; });
  ll p = -1;
  ll cnt = 0;
  for (ll i : ord) {
    if (p <= A[i]) {
      cnt++;
      p = B[i];
    }
  }
  cout << cnt << endl;


  return 0;
}

