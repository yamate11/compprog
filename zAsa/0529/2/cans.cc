int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, M; cin >> N >> M;
  // @InpGrid(N, M, B) [gsMEP5mb]
  auto B = vector(N, vector(M, ll()));
  for (int i = 0; i < N; i++) for (int j = 0; j < M; j++) { ll v; cin >> v; B[i][j] = v; }
  // @End [gsMEP5mb]

  bool b = FORALL(i, 0, N, B[0][0] + 7 * i == B[i][0]);
  bool c = FORALL(i, 0, N, FORALL(j, 0, M, B[i][j] == B[i][0] + j));
  ll x = B[0][0] % 7; if (x == 0) x = 7;
  ll y = B[0][M - 1] % 7; if (y == 0) y = 7;
  bool d = x <= y;
  cout << (b && c && d ? "Yes\n" : "No\n");

  return 0;
}

