// This is a very ad-hoc implementation...
istream& operator>>(istream& is, __int128& o) {
  string s; is >> s;
  o = 0;
  int sign = 1;
  for (int i = 0; i < ssize(s); i++) {
    if (s[i] == '-') {
      if (i == 0) sign = -1;
      else throw runtime_error(">>: misplaced minus sign");
    }else {
      int d = s[i] - '0';
      if (0 <= d and d <= 9) {
        o = 10 * o + d;
      }else throw runtime_error(">>: not a digit");
    }
  }
  o = sign * o;
  return is;
    
}

