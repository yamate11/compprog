//   queue, priority-queue

template<typename T>
string g_show_queue_and_like(const T& v0, auto front_like) {
  T v = v0;  // copy
  string ret = "[";
  bool first = true;
  while (not v.empty()) {
    if (not first) ret += ", ";
    first = false;
    const auto& x = front_like(v);
    ret += g_show(x);
    v.pop();
  }
  return ret;
}

template <typename T, typename T2>
string g_show(const queue<T, T2>& v) {
  return g_show_queue_and_like(v, [](const queue<T, T2>& vv) { return vv.front(); });
}

template <typename T, typename T2, typename T3>
string g_show(const priority_queue<T, T2, T3>& v) {
  return g_show_queue_and_like(v, [](const queue<T, T2>& vv) { return vv.top(); });
}

//    optional
template <typename T>
string g_show(const optional<T>& t) { return t ? g_show(*t) : "(nullopt)"; }

//    (signed/unsigned) __int128

string g_show(unsigned __int128 x) {
  if (x == 0) return "0";
  string s;
  while (x > 0) {
    int d = x % 10;
    s.push_back('0' + d);
    x /= 10;
  }
  reverse(s.begin(), s.end());
  return s;
}

string g_show(__int128 x) {
  if (x >= 0) {
    return g_show((unsigned __int128)x);
  } else {
    unsigned __int128 ux = (unsigned __int128)x;
    ux = ~ux + 1;
    return "-" + g_show(ux);
  }
}

