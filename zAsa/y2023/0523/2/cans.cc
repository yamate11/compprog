int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, L, W; cin >> N >> L >> W;
  // @InpVec(N, A) [7iqvRQSn]
  auto A = vector(N, ll());
  for (int i = 0; i < N; i++) { ll v; cin >> v; A[i] = v; }
  // @End [7iqvRQSn]
  A.push_back(L);

  ll ans = 0;
  ll cur = 0;
  ll i = 0;
  while (i <= N) {
    if (cur < A[i]) {
      ll n = divCeil(A[i] - cur, W);
      ans += n;
      cur += n * W;
    }else {
      cur = A[i] + W;
      i++;
    }
  }
  cout << ans << endl;

  return 0;
}

