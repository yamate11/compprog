int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  auto solve = [&]() -> bool {
    ll N; cin >> N;
    ll A, B; cin >> A >> B;
    if (A == B) return not (N % (A + 1) == 0);
    else if (A > B) return true;
    else {
      if (A >= N) return true;
      return false;
    }
    
  };
  cout << (solve() ? "Takahashi\n" : "Aoki\n");

  return 0;
}

