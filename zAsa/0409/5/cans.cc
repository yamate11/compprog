int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, M; cin >> N >> M;
  // @InpNbrList(N, M, nbr, dec=1, read=C, idx=idx) [DfqX5ko1]
  struct nbr_t {
    int nd;
    ll C;
    int idx;
    nbr_t(int nd_ = int(), ll C_ = ll(), int idx_ = 0) : nd(nd_), C(C_), idx(idx_) {}
  };
  auto nbr = vector(N, vector(0, nbr_t()));
  for (int i = 0; i < M; i++) {
    int u, v; cin >> u >> v; u -= 1; v -= 1;
    ll C; cin >> C;
    nbr[u].emplace_back(v, C, i);
    nbr[v].emplace_back(u, C, i);
  }
  // @End [DfqX5ko1]

  vector<ll> ans(N, -1LL);
  auto dfs = [&](auto rF, ll nd) -> void {
    for (auto [peer, c, _i] : nbr[nd]) {
      if (ans[peer] >= 0) continue;
      if (ans[nd] == c) ans[peer] = (c % N) + 1;
      else ans[peer] = c;
      rF(rF, peer);
    }
  };
  ans[0] = 1;
  dfs(dfs, 0);
  REPOUT(i, 0, N, ans[i], "\n");

  return 0;
}

