int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  double A, B, C; cin >> A >> B >> C;

  auto f = [&](double t) -> double {
    return A * t + B * sin(C * t * M_PI);
  };

  double d = 0.001;
  double t = 0.0;
  while (true) {
    if (f(t) < 100.0 and f(t + d) > 100.0) break;
    t += d;
  }
  auto check = [&](double x) -> bool {
    return f(x) < 100.0;
  };
  double x = binsearch<double>(check, t, t + d, 1e-10);
  cout << x << endl;

  return 0;
}

