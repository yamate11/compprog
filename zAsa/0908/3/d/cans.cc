int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  string S; cin >> S;
  ll N = SIZE(S);
  auto D = vector(10, 0LL);
  for (char c : S) D[c - '0']++;
  bool ans = false;
  if (N == 1) {
    ans = S == "8";
  }else if (N == 2) {
    ll d1 = (S[0] - '0') + 10 * (S[1] - '0');
    ll d2 = (S[1] - '0') + 10 * (S[0] - '0');
    ans = (d1 % 8 == 0) or (d2 % 8 == 0);
  }else {
    ans = false;
    for (ll x = 104; x < 1000; x += 8) {
      auto E = vector(10, 0LL);
      ll y = x;
      while (y > 0) {
        E[y % 10]++;
        y /= 10;
      }
      ans = FORALL(j, 0, 10, E[j] <= D[j]);
      if (ans) break;
    }
  }
  cout << (ans ? "Yes\n" : "No\n");
    

  return 0;
}

