int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll n, k; cin >> n >> k;
  vector<bool> A(1001);
  ll big = 1e9;
  ll pmin = big;
  ll mmax = -big;
  ll pmax = 0;
  ll mmin = 0;
  bool has_zero = false;
  vector<ll> Pos, Neg;
  for (ll i = 0; i < k; i++) {
    ll a; cin >> a;
    if (A[a]) continue;
    A[a] = true;
    ll b = a - n;
    if (b == 0) has_zero = true;
    if (b > 0) {
      pmin = min(b, pmin);
      pmax = max(b, pmax);
      Pos.push_back(b);
    }
    if (b < 0) {
      mmax = max(b, mmax);
      mmin = min(b, mmin);
      Neg.push_back(b);
    }
  }
  if (has_zero) {
    cout << 1 << endl;
    return 0;
  }
  if (pmin == big || mmax == -big) {
    cout << -1 << endl;
    return 0;
  }
  ll g0 = gcd(pmin, -mmax);
  ll lim = pmin / g0 + (-mmax) / g0;
  ll tblsz = 2 * (1000*1000 + 10000);
  vector<ll> tbl(tblsz, big);
  auto atbl = [&](ll x) -> ll& { return tbl[x + tblsz/2]; };
  atbl(0) = 0;
  auto doit = [&](ll x) -> void {
    DLOGK(x, lim, mmin, pmax, lim*mmin, lim*pmax);
    ll z = 1e9;
    if (x > 0) {
      for (ll y = lim * mmin; y <= lim * pmax; y++) {
	if (y + x == 0) {
	  z = atbl(y) + 1;
	}else {
	  updMin(atbl(y + x), atbl(y) + 1);
	}
      }
    }
    if (x < 0) {
      for (ll y = lim * pmax; y >= lim * mmin; y--) {
	if (y + x == 0) {
	  z = atbl(y) + 1;
	}else {
	  updMin(atbl(y + x), atbl(y) + 1);
	}
      }
    }
#if DEBUG
    for (ll y = lim * mmin; y <= lim * pmax; y++) {
      // if (atbl(y) < big) { DLOG("atbl: ", y, atbl(y)); }
    }
#endif
    DLOGK(z);
    lim = min(lim, z);
  };
  for (ll i = 0; i < (ll)Pos.size() || i < (ll)Neg.size(); i++) {
    if (i < (ll)Pos.size()) doit(Pos[i]);
    if (i < (ll)Neg.size()) doit(Neg[i]);
  }
  cout << lim << endl;

  return 0;
}

