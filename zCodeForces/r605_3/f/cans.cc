int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  string S, T; cin >> S >> T;
  ll N = S.size();
  ll M = T.size();
  ll L = max(N, M);
  const ll big = 1e10;
  vector tbl(N + 1, vector(M + 1, vector(L + 1, big)));
  for (ll k = 0; k <= L; k++) tbl[0][0][k] = k;
  for (ll i = 0; i <= N; i++) {
    for (ll j = 0; j <= M; j++) {
      for (ll k = 0; k <= L; k++) {
        for (char c : string("()")) {
          ll i0 = (i < N && S[i] == c) ? i+1 : i;
          ll j0 = (j < M && T[j] == c) ? j+1 : j;
          ll k0 = c == '(' ? k+1 : k-1;
          DLOGK(i,j,k,c,i0,j0,k0);
          if (i0 == i && j0 == j) continue;
          if (k0 < 0 || k0 > L) continue;
          updMin(tbl[i0][j0][k0], tbl[i][j][k] + 1);
          DLOGK(tbl[i0][j0][k0]);
        }
      }
    }
  }
  DLOGK(tbl);
  ll vmin = big;
  ll k1 = -1;
  for (ll k = 0; k <= L; k++) {
    if (tbl[N][M][k] + k < vmin) {
      vmin = tbl[N][M][k];
      k1 = k;
    }
  }
  ll i = N, j = M, k = k1;
  DLOGKL("init", i, j, k);
  string ans;
  while (i > 0 || j > 0) {
    auto [i0, j0, k0] = [&]() -> tuple<ll, ll, ll> {
      for (ll p = max(0LL, i - 1); p <= i; p++) {
        for (ll q = max(0LL, j - 1); q <= j; q++) {
          for (ll r = k - 1; r <= k + 1; r += 2) {
            if (r < 0 || r > L) continue;
            if (tbl[p][q][r] + 1 != tbl[i][j][k]) continue;
            char c = r == k - 1 ? '(' : ')';
            if (p < i && S[p] != c) continue;
            if (q < j && T[q] != c) continue;
            return {p, q, r};
          }
        }
      }
      DLOGK(i, j, k);
      assert(0);
    }();
    DLOGK(i,j,k,i0,j0,k0);
    char c = (i0 < i) ? S[i0] : T[j0];
    ans += c;
    i = i0; j = j0; k = k0;
  }
  reverse(ans.begin(), ans.end());
  for (ll x = 0; x < k; x++) cout << '(';
  cout << ans;
  for (ll x = 0; x < k1; x++) cout << ')';
  cout << endl;
  return 0;
}

