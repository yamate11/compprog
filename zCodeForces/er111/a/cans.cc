  auto solve = [&]() -> void {
    ll t; cin >> t;
    auto check = [&](ll n) -> bool {
      return n*n >= t;
    };
    ll n = binsearch_i<ll>(check, 5000, 0);
    cout << n << "\n";
  };

  ll T; cin >> T;
  for (ll t = 0; t < T; t++) solve();

