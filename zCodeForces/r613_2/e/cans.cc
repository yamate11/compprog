int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  auto solve = [&]() -> void {
    ll N; cin >> N;
    using sta = pair<ll, ll>;
    vector<sta> LR;
    using stb = tuple<ll, ll, ll>;
    vector<stb> ev;
    for (ll i = 0; i < N; i++) {
      ll l, r; cin >> l >> r;
      l = 3 * l - 1;
      r = 3 * r + 1;
      LR.emplace_back(l, r);
      ev.emplace_back(l, 1, i);
      ev.emplace_back(r, -1, i);
    }
    sort(LR.begin(), LR.end());
    sort(ev.begin(), ev.end());
    DLOG("LR=", LR);
    DLOG("ev=", ev);

    // ll evsz = ev.size();
    vector<ll> mul({0});
    vector<ll> cor({ll(-1e10)});
    ll cmul = 0;
    ll prev = -1e10;
    for (auto [t, q, i] : ev) {
      cmul += q;
      if (prev == t) {
	mul[mul.size() - 1] = cmul;
      }else {
	mul.push_back(cmul);
	cor.push_back(t);
      }
      prev = t;
    }
    DLOG("mul=", mul);

    ll mulsz = mul.size();
    ll orig = 1;
    for (ll i = 0; i < mulsz; i++) if (mul[i] == 0) orig++;
    orig -= 2;
    vector<ll> add(N);
    ll idx = 0;
    for (ll x = 0; x < mulsz; x++) {
      if (mul[x] == 1 && mul[x-1] >= 2 && mul[x+1] >= 2) {
	while (!(LR[idx].first <= cor[x] && cor[x+1] <= LR[idx].second)) idx++;
	add[idx]++;
      }
    }
    ll maxadd = 0;
    for (ll i = 0; i < N; i++) maxadd = max(maxadd, add[i]);
    auto isSingle = [&]() -> ll {
      for (ll x = 0; x < mulsz; x++) {
	if (mul[x] >= 2) return 0;
      }
      return 1;
    };

    ll single = isSingle();
    DLOG("orig=", orig, "maxadd=", maxadd, "single=", single);
    cout << orig + maxadd - single << "\n";
  };

  ll T; cin >> T;
  for (ll t = 0; t < T; t++) solve();

  return 0;
}

