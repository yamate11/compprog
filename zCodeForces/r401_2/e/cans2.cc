int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll n; cin >> n;
  using sta = tuple<ll, ll, ll>;
  vector<sta> rings;
  ll a0 = 1e18, b0 = 0, h0 = 0;
  for (ll i = 0; i <= n; i++) {
    ll a, b, h;
    if (i < n) {
      cin >> a >> b >> h;
    }else {
      a = -1; b = -1; h = -1;
    }
    if (b0 == 0 || b == b0) {
      b0 = b;
      h0 += h;
      a0 = min(a0, a);
    }else {
      rings.emplace_back(b0, a0, h0);
      a0 = a; b0 = b; h0 = h;
    }
  }
  using stb = pair<ll, ll>;
  vector<stb> stock;
  sort(rings.begin(), rings.end());
  DLOGK(rings);
  for (auto [b, a, h] : rings) {
    bool found = false;
    stb last;
    while (true) {
      if (stock.empty()) {
	break;
      }
      auto [bb, hh] = stock.back();
      if (bb <= a) {
	break;
      }
      found = true;
      last = stock.back();
      stock.pop_back();
    }
    if (found) {
      auto [bb, hh] = last;
      stock.emplace_back(b, hh + h);
    }
    stock.emplace_back(b, h);
    DLOGK(b,a,h);
    DLOGK(stock);
  }
  auto [bM, hM] = stock[0];
  cout << hM << endl;

  return 0;
}

