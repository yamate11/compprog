int naive(istream& cin, ostream& cout) {
  ll n, m; cin >> n >> m;
  vector<vector<ll>> A(n, vector(m, 0LL));
  for (ll i = 0; i < n; i++) {
    for (ll j = 0; j < m; j++) cin >> A[i][j];
  }
  ll k; cin >> k;
  for (ll i = 0; i < k; i++) {
    ll x, y; cin >> x >> y; x--; y--;
    bool found = false;
    for (ll j = 0; j < m; j++) {
      bool ok = true;
      for (ll p = x; p+1 <= y; p++) {
	if (A[p][j] > A[p+1][j]) {
	  ok = false;
	  break;
	}
      }
      if (ok) {
	found = true;
	break;
      }
    }
    if (found) cout << "Yes\n";
    else cout << "No\n";
  }

  return 0;
}
