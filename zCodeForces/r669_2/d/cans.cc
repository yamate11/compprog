int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N; cin >> N;
  vector<ll> H(N);
  using sta = pair<ll, ll>;
  vector<sta> Q;
  for (ll i = 0; i < N; i++) {
    cin >> H[i];
    Q.emplace_back(H[i], i);
  }
  sort(Q.begin(), Q.end());
  vector<ll> full(N);
  iota(full.begin(), full.end(), 0);
  set<ll> rem(full.begin(), full.end());
  vector<vector<ll>> nxt(N);
  for (ll i = 0; i < N; i++) nxt[i].push_back(i+1);

  auto func1 = [&](ll j) -> pair<ll, vector<ll>> {
    vector<ll> toproc;
    auto [ht, i] = Q[j];
    while (true) {
      toproc.push_back(i);
      rem.erase(i);
      if (j + 1 == N) break;
      auto [ht2, i2] = Q[j+1];
      if (ht2 != ht) break;
      j++;
      i = i2;
    }
    return {j + 1, move(toproc)};
  };

  auto func2 = [&]() -> void {
    for (ll j = 0; j < N; ) {
      auto [nxtJ, toproc] = func1(j);
      DLOG("func2: j=", j, "nxtJ=", nxtJ, "toproc=", toproc, "rem=", rem);
      for (ll i : toproc) {
	auto it = rem.lower_bound(i);
	if (it == rem.end() || it == rem.begin()) continue;
	ll q = *it;
	it--;
	nxt[*it].push_back(q);
      }
      j = nxtJ;
    }
  };

  func2();
  reverse(Q.begin(), Q.end());
  rem = set<ll>(full.begin(), full.end());
  DLOG("reversed, Q=", Q);
  func2();
  
  DLOG("nxt=", nxt);

  ll big = 1e9;
  vector<ll> tbl(N, big);
  tbl[N-1] = 0;
  for (ll i = N-2; i >= 0; i--) {
    for (ll x : nxt[i]) updMin(tbl[i], tbl[x] + 1);
  }
  cout << tbl[0] << endl;

  return 0;
}

