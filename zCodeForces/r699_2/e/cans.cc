int naive(istream& cin, ostream& cout) {
  ll n; cin >> n;
  vector<ll> A(n);
  vector<ll> cnt(n + 1);
  for (ll i = 0; i < n; i++) {
    ll a; cin >> a;
    A[i] = a;
    cnt[a]++;
  }
  ll c = A[n - 1];
  for (ll i = n - 1; i >= 0 && A[i] == A[n - 1]; i--) cnt[c]--;
  ll ans = 1e10;
  for (ll x = 0; x < (1LL << n); x++) {
    ll prev = -1;
    vector<bool> fin(n);
    bool fail = false;
    for (ll j = 0; j < n; j++) {
      ll cur = A[j] - 1;
      if (!((x >> cur) & 1)) continue;
      if (cur == prev) continue;
      if (fin[cur]) {
        fail = true;
        DLOGKL("fail", x);
        break;
      }
      if (prev >= 0) {
        fin[prev] = true;
      }
      prev = cur;
    }
    if (fail) continue;
    ll t = 0;
    for (ll j = 0; j < n; j++) {
      if (!((x >> j) & 1)) t += cnt[j + 1];
    }
    DLOGK(x, t);
    ans = min(ans, t);
  }
  cout << ans << endl;
  return 0;
}
int body(istream& cin, ostream& cout) {

  ll n0; cin >> n0;
  vector<ll> A(n0);
  vector<ll> c_st(n0 + 1, -1LL), c_en(n0 + 1, -1LL), c_num(n0 + 1, 0LL);
  for (ll i = 0; i < n0; i++) cin >> A[i];
  ll n1 = n0 - 1;
  for ( ; n1 - 1 >= 0 && A[n1 - 1] == A[n0 - 1]; n1--);
  if (n1 == 0) {
    cout << 0 << endl;
    return 0;
  }
  DLOGK(A);
  for (ll i = 0; i < n0; i++) {
    ll c = A[i];
    if (c_st[c] < 0) c_st[c] = i;
    c_en[c] = i;
    c_num[c]++;
  }
  c_num[A[n0 - 1]] -= (n0 - n1);
  vector<bool> b_en(n0);
  for (ll i = 1; i <= n0; i++) {
    if (c_en[i] >= 0) b_en[c_en[i]] = true;
  }
  DLOGK(c_st);
  DLOGK(c_en);
  DLOGK(c_num);
  DLOGK(b_en);
  vector<ll> tbl(n0 + 1);
  for (ll i = 0; i < n0; i++) {
    if (b_en[i]) {
      ll c = A[i];
      ll j = c_st[c];
      tbl[i + 1] = max(tbl[i], tbl[j] + c_num[c]);
    }else {
      tbl[i + 1] = tbl[i];
    }
  }
  cout << n1 - tbl[n0] << endl;

  return 0;
}

