int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  auto start = std::chrono::system_clock::now();

  ll n, m, k, s; cin >> n >> m >> k >> s;
  vector<ll> A(n + 1);
  vector<vector<ll>> has(k + 1);
  for (ll i = 1; i <= n; i++) {
    cin >> A[i];
    has[A[i]].push_back(i);
  }

  auto pt1 = std::chrono::system_clock::now();

  vector<vector<ll>> nbr(n + 1);
  for (ll i = 0; i < m; i++) {
    ll u, v; cin >> u >> v;
    nbr[u].push_back(v);
    nbr[v].push_back(u);
  }
  DLOGK(nbr);

  auto pt2 = std::chrono::system_clock::now();

  vector<vector<int>> dist(k + 1, vector<int>(n + 1, -1));
  using q_t = int;
  for (ll i = 1; i <= k; i++) {
    queue<q_t> que;
    for (int twn : has[i]) {
      dist[i][twn] = 0;
      que.emplace(twn);
    }
    DLOGK(i, has[i]);
    DLOGK(i, que);
    while (!que.empty()) {
      int twn= que.front(); que.pop();
      for (int tn : nbr[twn]) {
        if (dist[i][tn] < 0) {
          dist[i][tn] = dist[i][twn] + 1;
          que.emplace(tn);
        }
      }
    }
    DLOGK(i, dist);
  }

  auto pt3 = std::chrono::system_clock::now();

  for (ll tn = 1; tn <= n; tn++) {
    vector<ll> vec(k);
    for (ll i = 0; i < k; i++) vec[i] = dist[i + 1][tn];
    sort(vec.begin(), vec.end());
    ll cost = 0;
    for (ll i = 0; i < s; i++) cost += vec[i];
    if (tn > 1) cout << " ";
    cout << cost;
  }
  cout << "\n";

  auto pt4 = std::chrono::system_clock::now();

  cerr << "pt1: " << duration_cast<milliseconds>(pt1 - start).count() << "\n";
  cerr << "pt2: " << duration_cast<milliseconds>(pt2 - start).count() << "\n";
  cerr << "pt3: " << duration_cast<milliseconds>(pt3 - start).count() << "\n";
  cerr << "pt4: " << duration_cast<milliseconds>(pt4 - start).count() << "\n";

  return 0;
}

