int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);
  
  CoordCompr cc;
  ll N, Q; cin >> N >> Q;
  vector<ll> A(N); cin >> A;
  for (ll x : A) cc.add(x);
  ll NQ = max(N, Q);
  ll K = pow(NQ, 0.31);
  ll bsz = (NQ + K - 1) / K;

  using query_t = tuple<ll, ll, ll, ll>;
  auto query = vector(K, vector(K, vector<query_t>()));
  using change_t = tuple<ll, ll, ll>;
  vector<change_t> change;
  auto tmpA(A);
  ll tm = 0;
  ll qidx = 0;
  for (ll q = 0; q < Q; q++) {
    ll tp; cin >> tp;
    if (tp == 1) {
      ll l, r; cin >> l >> r; l--;
      query[tm / bsz][l / bsz].emplace_back(r, tm, l, qidx++);
    } else if (tp == 2) {
      ll p, x; cin >> p >> x; p--;
      change.emplace_back(p, tmpA[p], x);
      tm++;
      tmpA[p] = x;
      cc.add(x);
    }
  }
  for (ll& x : A) x = cc.c(x);
  for (auto& [p, x1, x2] : change) { x1 = cc.c(x1); x2 = cc.c(x2); }
  vector<ll> num_appear(cc.size());
  vector<ll> count(N + 1);
  count[0] = 1e9;
  ll tmplim = sqrt(N) + 10;
  set<ll> zeros;
  for (ll i = 1; i <= tmplim; i++) zeros.insert(i);
  ll curL = 0;
  ll curR = 0;
  ll curT = 0;
  auto add = [&](ll x) -> void {
    ll a = ++num_appear[x];
    if (--count[a-1] == 0) zeros.insert(a-1);
    if (count[a]++ == 0) zeros.erase(a);
  };
  auto remove = [&](ll x) -> void {
    ll a = --num_appear[x];
    if (--count[a+1] == 0) zeros.insert(a+1);
    if (count[a]++ == 0) zeros.erase(a);
  };
  auto moveT = [&](ll newT) -> void {
    auto from_to = [&](ll p, ll x1, ll x2) -> void {
      DLOGK("..moveT..", curT, newT, p, x1, x2);
      assert(A[p] == x1);
      if (curL <= p && p < curR) {
        remove(A[p]);
        add(x2);
      }
      A[p] = x2;
    };

    if (curT < newT) {
      for (ll t = curT; t < newT; t++) {
        auto [p, x1, x2] = change[t];
        from_to(p, x1, x2);
      }
    }else if (newT < curT) {
      for (ll t = curT - 1; t >= newT; t--) {
        auto [p, x2, x1] = change[t];
        from_to(p, x1, x2);
      }
    }
    curT = newT;
  };
  auto moveL = [&](ll newL) -> void {
    if (curL < newL) {
      for (ll l = curL; l < newL; l++) remove(A[l]);
    }else if (newL < curL) {
      for (ll l = curL - 1; l >= newL; l--) add(A[l]);
    }
    curL = newL;
    DLOGKL("moveL", newL, num_appear, count);
  };
  auto moveR = [&](ll newR) -> void {
    if (curR < newR) {
      for (ll r = curR; r < newR; r++) add(A[r]);
    }else if (newR < curR) {
      for (ll r = curR - 1; r >= newR; r--) remove(A[r]);
    }
    curR = newR;
    DLOGKL("moveR", newR, num_appear, count);
  };
  vector<ll> ans(qidx);
  for (ll gt = 0; gt < K; gt++) {
    for (ll gl = 0; gl < K; gl++) {
      auto qs = query[gt][gl];
      sort(qs.begin(), qs.end());
      for (auto [r, t, l, qi] : qs) {
        DLOGKL("query", t, l, r, qi);
        moveT(t);
        if (r < curL) { moveL(l); moveR(r); }
        else          { moveR(r); moveL(l); }
        ans[qi] = *zeros.begin();
      }
    }
  }
  for (ll x : ans) cout << x << "\n";


  return 0;
}

