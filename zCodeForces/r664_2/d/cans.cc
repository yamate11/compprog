int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll n, d, m; cin >> n >> d >> m;
  vector<ll> ba, sa;
  for (ll i = 0; i < n; i++) {
    ll a; cin >> a;
    if (a > m) ba.push_back(a);
    else       sa.push_back(a);
  }
  sort(ba.begin(), ba.end(), greater<ll>());
  sort(sa.begin(), sa.end(), greater<ll>());
  DLOG("ba=", ba);
  DLOG("sa=", sa);
  ll q = ba.size();
  ll p = sa.size();
  ll ans = 0;
  vector<ll> b_acc(q+1), s_acc(p+1);
  for (ll i = 0; i < q; i++) b_acc[i+1] = b_acc[i] + ba[i];
  for (ll i = 0; i < p; i++) s_acc[i+1] = s_acc[i] + sa[i];
  for (ll s1 = 0; s1 <= p; s1++) {
    ll k = divCeil(n - s1, d + 1);
    if (k > q) continue;
    ans = max(ans, b_acc[k] + s_acc[s1]);
  }
  cout << ans << endl;

  return 0;
}

