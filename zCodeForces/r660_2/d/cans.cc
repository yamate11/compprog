int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N; cin >> N;
  vector<ll> A(N), B(N), val(N);
  vector<vector<ll>> rev(N);
  vector<ll> roots;
  for (ll i = 0; i < N; i++) cin >> A.at(i);
  for (ll i = 0; i < N; i++) {
    ll b; cin >> b; b--;
    if (b < 0) {
      B.at(i) = -1;
      roots.push_back(i);
    } else {
      B.at(i) = b;
      rev.at(b).push_back(i);
    }
  }
  DLOG("rev=", rev);
  auto ask = [&](auto rF, ll i) -> ll {
    // seenA.at(i) = true;
    ll v = A.at(i);
    for (ll j : rev.at(i)) {
      ll w = rF(rF, j);
      if (w > 0) v += w;
    }
    val.at(i) = v;
    return v;
  };
  for (ll i : roots) {
    // if (seenA.at(i)) continue;
    ask(ask, i);
  }
  DLOG("val=", val);
  ll ans = 0;
  vector<ll> P;
  auto opexec = [&](auto rF, ll i) -> void {
    // seenB.at(i) = true;
    DLOG("opexec called, i=", i);
    for (ll j : rev.at(i)) {
      if (val.at(j) > 0) rF(rF, j);
    }
    ans += val.at(i);
    P.push_back(i);
    DLOG("P.pushback: ", i);
    for (ll j : rev.at(i)) {
      if (val.at(j) <= 0) rF(rF, j);
    }
  };
  for (ll i : roots) {
    // if (seenB.at(i)) continue;
    opexec(opexec, i);
  }
  DLOG("P=", P);
  cout << ans << endl;
  for (ll i = 0; i < N; i++) {
    if (i > 0) cout << " ";
    cout << P.at(i) + 1;
  }
  cout << endl;

  return 0;
}

