int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll n; cin >> n;
  ll lim = 1e7;
  ll amax = 0;
  vector<ll> vec1(lim + 1, -1), vec2(lim + 1, -1);
  for (ll i = 0; i < n; i++) {
    ll a; cin >> a;
    if (vec1[a] < 0) vec1[a] = i;
    else if (vec2[a] < 0) vec2[a] = i;
    amax = max(amax, a);
  }
  ll ans = 1e15;
  ll ans1 = -1, ans2 = -1;
  for (ll g = 1; g <= amax; g++) {
    if (ans < g) break;
    ll p1 = -1, p2 = -1;
    ll a1 = -1, a2 = -1;
    for (ll x = g; x <= min(amax, ans); x += g) {
      DLOG("trying x=", x);
      if (vec1[x] < 0) continue;
      DLOG("vec1[x]=", vec1[x]);
      if (p1 < 0) {
	if (vec2[x] >= 0) {
	  p1 = vec1[x];
	  p2 = vec2[x];
	  a1 = a2 = x;
	  break;
	}else {
	  p1 = vec1[x];
	  a1 = x;
	}
      }else {
	p2 = vec1[x];
	a2 = x;
	break;
      }
    }
    ll cand = a1 * a2 / g;
    if (p2 >= 0 && ans > cand) {
      if (p1 > p2) swap(p1, p2);
      ans1 = p1;
      ans2 = p2;
      ans = cand;
      DLOG("newans=", ans);
    }
  }
  cout << ans1 + 1 << " " << ans2 + 1 << endl;


  return 0;
}

