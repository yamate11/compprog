int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  auto solve = [&]() -> void {
    ll n, m; cin >> n >> m;
    IPoint p1, p2, p3, p4; cin >> p1 >> p2 >> p3 >> p4;
    p1.x--;
    p1.y--;
    p3.x--;
    p3.y--;
    ll s1 = 0, s2 = p3.x, s3 = p4.x, s4 = m;
    ll t1 = 0, t2 = p3.y, t3 = p4.y, t4 = n;

    auto func2 = [&](ll sA, ll tA, ll sB, ll tB) -> ll {
      ll ss = sB - sA;
      ll tt = tB - tA;
      ll p = ss * tt / 2;
      if (ss % 2 == 0 || tt % 2 == 0) return p;
      if ((sA + tA) % 2 == 1) return p;
      else return p + 1;
    };

    auto func = [&](ll sA, ll tA, ll sB, ll tB) -> ll {
      /*
      ll ss = sB - sA;
      ll tt = tB - tA;
      */
      ll x1 = max(p1.x, sA);
      ll y1 = max(p1.y, tA);
      ll x2 = min(p2.x, sB);
      ll y2 = min(p2.y, tB);

      ll w_under = func2(sA, tA, sB, tB);
      if (x1 >= x2 || y1 >= y2) {
        DLOGKL("non-overwrap", sA, tA, sB, tB, x1, y1, x2, y2, w_under);
        return w_under;
      }
      ll w_over = func2(x1, y1, x2, y2);
      // ll b_over = (x2 - x1) * (y2 - y1) - w_over;
      ll ret = (w_under - w_over) + (x2 - x1) * (y2 - y1);
      DLOGK(sA, tA, sB, tB, x1, y1, x2, y2, w_under, w_over, ret);
      return ret;
    };

    ll w1 = func(s1, t1, s2, t2);
    ll w2 = func(s2, t1, s3, t2);
    ll w3 = func(s3, t1, s4, t2);
    ll w4 = func(s1, t2, s2, t3);
    ll w5 = func(s3, t2, s4, t3);
    ll w6 = func(s1, t3, s2, t4);
    ll w7 = func(s2, t3, s3, t4);
    ll w8 = func(s3, t3, s4, t4);
    DLOGK(w1, w2, w3);
    DLOGK(w4, w5);
    DLOGK(w6, w7, w8);
    ll w = w1 + w2 + w3 + w4 + w5 + w6 + w7 + w8;
    ll b = n * m - w;
    cout << w << " " << b << "\n";

  };

  ll T; cin >> T;
  for (ll t = 0; t < T; t++) solve();

  return 0;
}

