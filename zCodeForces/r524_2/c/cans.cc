int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  auto solve = [&]() -> void {
    ll n, m; cin >> n >> m;

    auto countW = [&](ll p1, ll q1, ll p2, ll q2) -> ll {
      ll pp = p2 - p1;
      ll qq = q2 - q1;
      ll tot = pp * qq;
      ll ret = tot / 2;
      if (pp % 2 == 1 && qq % 2 == 1 && p1 % 2 == q1 % 2) ret++;
      DLOGKL("countW", p1, q1, p2, q2, ret);
      return ret;
    };
    auto countB = [&](ll p1, ll q1, ll p2, ll q2) -> ll {
      ll pp = p2 - p1;
      ll qq = q2 - q1;
      ll tot = pp * qq;
      ll ret = tot - countW(p1, q1, p2, q2);
      DLOGKL("countB", p1, q1, p2, q2, ret);
      return ret;
    };
    ll x1, y1, x2, y2; cin >> x1 >> y1 >> x2 >> y2; x1--; y1--;
    ll x3, y3, x4, y4; cin >> x3 >> y3 >> x4 >> y4; x3--; y3--;
    ll w1 = countW(0, 0, m, n);
    ll w2 = countB(x1, y1, x2, y2);
    ll w3 = countW(x3, y3, x4, y4);
    ll xL = max(x1, x3);
    ll xH = min(x2, x4);
    ll yL = max(y1, y3);
    ll yH = min(y2, y4);
    ll w4 = (xL < xH && yL < yH)? countB(xL, yL, xH, yH) : 0;
    ll w = w1 + w2 - w3 - w4;
    ll b = m * n - w;
    DLOGK(w1, w2, w3, w4, w, b);
    cout << w << " " << b << "\n";
  };
    
  ll T; cin >> T;
  for (ll t = 0; t < T; t++) solve();

  return 0;
}

