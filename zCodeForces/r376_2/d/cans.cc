struct MyExc : exception {};

int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll n, c; cin >> n >> c;
  vector<vector<ll>> W(n);
  for (ll i = 0; i < n; i++) {
    ll l; cin >> l;
    for (ll j = 0; j < l; j++) {
      ll x; cin >> x; x--;
      W[i].push_back(x);
    }
  }

  using sta = pair<ll, bool>;
  vector<sta> rem_ev;
  rem_ev.emplace_back(0, true);
  rem_ev.emplace_back(c, false);
  auto reduce = [&](const vector<sta>& ev) -> ll {
    vector<sta> tmp = move(rem_ev);
    for (const sta& x : ev) tmp.push_back(x);
    sort(tmp.begin(), tmp.end());
    rem_ev = vector<sta>();
    ll cnt = 0;
    ll empty = true;
    ll sample = -1;
    for (auto [t, sw] : tmp) {
      if (sw) {
	if (cnt == 1) {
	  rem_ev.emplace_back(t, true);
	  sample = t;
	  empty = false;
	}
	cnt++;
      }else {
	if (cnt == 2) {
	  rem_ev.emplace_back(t, false);
	}
	cnt--;
      }
    }
    if (empty) throw MyExc();
    DLOG("reduce: rev_ev=", rem_ev);
    return sample;
  };
  auto func = [&](auto rF, ll u, ll v, ll k) -> void {
    if (v == u + 1) return;
    DLOG("func, u=", u, "v=", v, "k=", k);
    ll p_start = -1;
    ll p_cur = -1;
    ll i = u;
    while (i < v) {
      if ((ll)W[i].size() <= k) {
	if (p_start >= 0) throw MyExc();
	i++;
      }else {
	ll i0 = i;
	ll q = W[i][k];
	if (p_start < 0) p_start = p_cur = q;
	else {
	  p_cur += (10 * c + q - p_cur) % c;
	  if (p_cur >= p_start + c) throw MyExc();
	}
	while (i < v && (ll)W[i].size() > k && W[i][k] == q) i++;
	rF(rF, i0, i, k+1);
      }
    }
    p_cur++;
    if (p_cur <= c) {
      reduce(vector<sta>({{0, true}, {c - p_cur + 1, false},
				       {c - p_start, true}, {c, false}}));
    }else {
      reduce(vector<sta>({{c - p_start, true}, {2 * c - p_cur + 1, false}}));
    }

  };
  try {
    func(func, 0, n, 0);
    ll x = reduce(vector<sta>({{0, true}, {c, false}}));
    cout << x << endl;
  }catch(const MyExc& e) {
    cout << -1 << endl;
  }

  return 0;
}

