int naive(istream& cin, ostream& cout) {
  ll n, l, w0; cin >> n >> l >> w0;
  vector<ll> X(n), V(n);
  for (ll i = 0; i < n; i++) {
    ll x, v; cin >> x >> v;
    X[i] = x;
    V[i] = v;
  }
  ll ans = 0;
  for (ll i = 0; i < n; i++) {
    for (ll j = i + 1; j < n; j++) {
      if (V[i] == V[j]) continue;
      ll x0 = X[i], x1 = X[j];
      if (V[i] == -1) swap(x0, x1);
      if (x1 < x0) continue;
      double t = (x1 - x0) / 2.0;
      double cL = x0 + t * (1 - w0);
      if (llround(2 * cL) >= 0) continue;
      double cR = x0 + t * (1 + w0);
      if (llround(2 * (cR + l)) <= 0) continue;
      ans++;
    }
  }
  cout << ans << endl;

  return 0;
}
int body(istream& cin, ostream& cout) {
  // ll big = 1e9;
  ll n, l, w0; cin >> n >> l >> w0;
  vector<ll> left, right;
  for (ll i = 0; i < n; i++) {
    ll x, v; cin >> x >> v;
    if (v > 0) left.push_back(x);
    else right.push_back(x);
  }
  sort(right.begin(), right.end());
  ll sz = right.size();
  DLOGK(right);
  vector<ll> cond1, cond2;
  ll ans = 0;
  for (ll x0 : left) {

    ll i0 = upper_bound(right.begin(), right.end(), x0) - right.begin();
    DLOGK(x0, i0);
    if (i0 == sz) continue;

    auto check1 = [&](ll i) -> bool {
      ll x1 = right[i];
      ll t_2 = x1 + l - x0;
      ll pt = 2 * x0 + (1 - w0) * t_2;
      return pt < 0;
    };
    auto check2 = [&](ll i) -> bool {
      ll x1 = right[i];
      ll t_2 = x1 + l - x0;
      ll pt = 2 * x0 + (1 + w0) * t_2;
      return 0 < pt;
    };

    if (x0 < 0) {
      ll idx = binsearch<ll>(check2, sz, i0 - 1);
      DLOGK(x0, idx);
      if (idx >= i0) ans += sz - idx;
    }else {
      ll idx = binsearch<ll>(check1, sz, i0 - 1);
      DLOGK(x0, idx);
      if (idx >= i0) ans += sz - idx;
    }
  }
  cout << ans << endl;

  return 0;
}

