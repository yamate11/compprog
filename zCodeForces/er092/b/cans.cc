int main(int argc, char *argv[]) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  auto solve = [&](ll n, ll k, ll z, vector<ll>& A) -> ll {
    vector<ll> vec(z+1, 0);
    vec.at(0) = A.at(0);
    for (ll i = 1; i <= k; i++) {
      auto prev = move(vec);
      vec = vector<ll>(z+1, 0);
      for (ll p = 0; p <= z; p++) {
	ll f;
	ll b;
	if (i - 2 * p >= 0) {
	  f = prev.at(p) + A.at(i - 2 * p);
	  if (p-1 >= 0) b = prev.at(p-1) + A.at(i - 2 * p);
	  else          b = 0;
	  DLOG("i=", i, "p=", p, "f=", f, "b=", b);
	  vec.at(p) = max(f, b);
	}
      }
      DLOG("i=", i, "vec=", vec);
    }
    ll ans = 0;
    for (ll p = 0; p <= z; p++) {
      ans = max(ans, vec.at(p));
    }
    return ans;
  };

  ll T; cin >> T;
  for (ll t = 0; t < T; t++) {
    ll n, k, z; cin >> n >> k >> z;
    vector<ll> A(n);
    for (ll i = 0; i < n; i++) cin >> A.at(i);
    cout << solve(n, k, z, A) << endl;
  }

  return 0;
}

