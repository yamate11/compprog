int body(istream& cin, ostream& cout) {

  ll n, w, k; cin >> n >> w >> k;
  vector<ll> A(n), T(n);
  for (ll i = 0; i < n; i++) cin >> A[i];
  for (ll i = 0; i < n; i++) cin >> T[i];
  multiset<ll> half;
  multiset<ll, greater<ll>> full;
  ll tot = 0;
  auto f_half = [&](ll a) -> ll { return (a + 1) / 2; };
  auto add = [&](ll a) -> bool {
    if ((ll)half.size() < w) {
      if (tot + f_half(a) > k) return false;
      half.insert(a);
      tot += f_half(a);
      return true;
    }else {
      ll min_half = *half.begin();
      if (min_half < a) {
	ll diff = f_half(a) - f_half(min_half) + min_half;
	if (tot + diff > k) return false;
	half.erase(half.begin());
	full.insert(min_half);
	half.insert(a);
	tot += diff;
	return true;
      }else {
	if (tot + a > k) return false;
	full.insert(a);
	tot += a;
	return true;
      }
    }
  };
  auto remove = [&](ll a) -> void {
    auto remove_from = [&](auto& mset, ll value, ll tval) -> void {
      auto it = mset.find(value);
      assert(it != mset.end());
      mset.erase(it);
      tot -= tval;
    };
    if (full.empty()) {
      remove_from(half, a, f_half(a));
    }else {
      ll max_full = *full.begin();
      if (max_full >= a) {
	remove_from(full, a, a);
      }else {
	remove_from(half, a, f_half(a));
	full.erase(full.begin());
	half.insert(max_full);
	tot += f_half(max_full) - max_full;
      }
    }
  };
  ll i = 0;
  ll cur = 0;
  ll ans = 0;
  for (ll j = 0; j < n; j++) {
    for (; i < n && add(T[i]); i++) cur += A[i];
    DLOG("j=", j, "half=", half, "full=", full);
    ans = max(ans, cur);
    if (i == n) break;
    if (i > j) {
      remove(T[j]);
      cur -= A[j];
    }else {
      i++;
    }
  }
  cout << ans << endl;

  return 0;
}

