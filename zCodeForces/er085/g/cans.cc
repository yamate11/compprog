vector<int> zAlg(string s, string alt) {
  int n = s.size();
  vector<int> z(n);
  z.at(0) = n;
  int i = 1;
  int j = 0;
  while (i < n) {
    for ( ; i+j < n && (s.at(j) == s.at(i+j) || alt.at(j) == s.at(i+j)); j++);
    z.at(i++) = j;
    if (j > 0) {
      for (int k = 1; i < n && z.at(k) < j-1; k++, j--, i++) z.at(i) = z.at(k);
      j--;
    }
  }
  DLOG("s=", s, "alt=", alt, "z=", z);
  return z;
}

// matches("abab", "xababab") --> [1,3]
vector<int> matches(string p, string p2, string s, char sepChar = '\xff') {
  auto v = zAlg(p + string(1, sepChar) + s,
		p2 + string(1 + s.size(), sepChar));
  int n = p.size();
  vector<int> res;
  for (size_t i = 0; i < s.size() - (n-1); i++) {
    if (v.at(n+1 + i) == n) res.push_back(i);
  }
  return res;
}

