int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll n; cin >> n;
  vector<ll> X(n), Y(n);
  ll seqX = 0, seqY = 0;
  map<ll, ll> mpX, mpY;
  for (ll i = 0; i < n; i++) {
    ll x, y; cin >> x >> y;
    X[i] = x;
    Y[i] = y;
    if (mpX.find(x) == mpX.end()) {
      mpX[x] = seqX++;
    }
    if (mpY.find(y) == mpY.end()) {
      mpY[y] = seqY++;
    }
  }
  DLOGK(mpX);
  DLOGK(mpY);
  auto encX = [&](ll x) -> ll { return 0    + mpX[x]; };
  auto encY = [&](ll y) -> ll { return seqX + mpY[y]; };
  UnionFind uf(seqX + seqY);
  for (ll i = 0; i < n; i++) {
    uf.merge(encX(X[i]), encY(Y[i]));
  }
  vector<ll> ecount(2 * n);
  for (ll i = 0; i < n; i++) {
    ll e = uf.leader(encX(X[i]));
    ecount[e]++;
  }
  Fp ans = 1;
  for (const auto& [e, es] : uf.groups()) {
    
    ll gsz = uf.groupSize(e);
    ll ec = ecount[e];
    if (ec < gsz) {
      assert(ec + 1 == gsz);
      ans *= power<Fp>(2, gsz) - 1;
    }else {
      ans *= power<Fp>(2, gsz);
    }
  }
  cout << ans << endl;


  return 0;
}

