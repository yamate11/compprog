int main(int argc, char *argv[]) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  DLOG("p0");
  ll n, k; cin >> n >> k;
  DLOG("n=", n, "k=", k);
  vector nm(k+1, 0LL);
  for (ll i = 0; i < n; i++) {
    ll m; cin >> m;
    nm.at(m)++;
  }
  DLOG("nm=", nm);
  vector c(k+1, 0LL);
  for (ll i = 1; i <= k; i++) cin >> c.at(i);
  DLOG("c=", c);
  ll s = 0;
  ll vmax = 0;
  for (ll i = k; i >= 1; i--) {
    s += nm.at(i);
    vmax = max(vmax, (s + c.at(i) - 1) / c.at(i));
  }
  DLOG("p1");
  vector ans(vmax, vector<ll>());
  {
    ll p = 0;
    for (ll i = k; i >= 1; i--) {
      for (ll _j = 0; _j < nm.at(i); _j++) {
	ans.at(p).push_back(i);
	p = (p + 1) % vmax;
      }
    }
  }
  cout << vmax << endl;
  for (ll p = 0; p < vmax; p++) {
    cout << ans.at(p).size();
    for (ll v : ans.at(p)) cout << " " << v;
    cout << "\n";
  }

  
  

  return 0;
}

