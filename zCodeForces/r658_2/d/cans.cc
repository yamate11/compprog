int main(int argc, char *argv[]) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  auto solve = [&](ll n, auto& ps) -> bool {
    vector<ll> rec;
    {
      ll i = 0;
      ll cur = 0;
      ll cur_i = 0;
      for (; i <= 2*n; i++) {
	if (i == 2*n || ps.at(i) > cur) {
	  // DLOG("  i=", i, "ps.at(i)=", ps.at(i), "cur=", cur);
	  if (i > 0) rec.push_back(i - cur_i);
	  if (i == 2*n) break;
	  cur_i = i;
	  cur = ps.at(i);
	}
      }
    }
    DLOG("rec", rec);
    ll m = rec.size();
    vector cur(n+1, false);
    cur.at(0) = true;
    for (ll i = 0; i < m; i++) {
      vector prev = move(cur);
      cur = vector(n+1, false);
      for (ll j = n; j >= 0; j--) {
	if (prev.at(j)) {
	  cur.at(j) = true;
	  ll shift = j + rec.at(i);
	  if (shift <= n) cur.at(shift) = true;
	}
      }
      if (cur.at(n)) return true;
    }
    return false;
  };

  ll T; cin >> T;
  for (ll t = 0; t < T; t++) {
    ll n; cin >> n;
    vector<ll> ps(2*n);
    for (ll i = 0; i < 2*n; i++) cin >> ps.at(i);
    cout << (solve(n, ps) ? "YES" : "NO") << "\n";
  }

  return 0;
}

