int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll n; cin >> n;
  vector nm(2, 0LL);
  vector move(2, vector<ll>());
  for (ll i = 0; i < 2; i++) {
    ll k; cin >> k;
    nm[i] = k;
    move[i] = vector<ll>(k);
    for (ll j = 0; j < k; j++) {
      ll x; cin >> x;
      move[i][j] = x;
    }
  }
  vector winner(2, vector(n, -1));
  vector tbl(2, vector(n, 0LL));
  auto fill = [&](auto rF, ll p, ll i, ll w) -> void {
    DLOGKL("fill_called", p, i, w);
    winner[p][i] = w;
    for (ll j : move[1 - p]) {
      ll jj = (n + i - j) % n;
      if (winner[1 - p][jj] >= 0) continue;
      if (1 - p == w) {
	rF(rF, 1 - p, jj, w);
      }else {
	tbl[1 - p][jj]++;
	if (tbl[1 - p][jj] == nm[1 - p]) {
	  rF(rF, 1 - p, jj, w);
	}
      }
    }
  };
  winner[0][0] = 1;
  winner[1][0] = 0;
  fill(fill, 0, 0, 1);
  fill(fill, 1, 0, 0);
  for (ll p = 0; p < 2; p++) {
    for (ll i = 1; i < n; i++) {
      if (i > 1) cout << " ";
      ll w = winner[p][i];
      cout << (w == p ? "Win" : w == -1 ? "Loop" : "Lose");
    }
    cout << endl;
  }
    


  return 0;
}

