int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  auto solve = [&]() -> void {
    ll N, A, B; cin >> N >> A >> B;
    ll ans;
    if (3 * A <= B) {
      ans = N * A;
    }else {
      ll x = N / 3;
      ll y = N % 3;
      if (y == 0) ans = x * B;
      else if (y == 1) ans = min((x + 1) * B, x * B + A);
      else if (y == 2) ans = min((x + 1) * B, x * B + 2 * A);
    }
    cout << ans << "\n";
  };

  ll T; cin >> T;
  for (ll _t = 0; _t < T; _t++) solve();

  return 0;
}

