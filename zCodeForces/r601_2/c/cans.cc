int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll n; cin >> n;
  using sta = pair<ll, ll>;
  vector<vector<sta>> rec(n);
  for (ll i = 0; i < n - 2; i++) {
    vector<ll> p(3);
    for (ll j = 0; j < 3; j++) {
      ll a; cin >> a; a--;
      p[j] = a;
    }
    rec[p[0]].emplace_back(p[1], p[2]);
    rec[p[1]].emplace_back(p[2], p[0]);
    rec[p[2]].emplace_back(p[0], p[1]);
  }
  vector<ll> ones, twos;
  for (ll i = 0; i < n; i++) {
    if (rec[i].size() == 1) ones.push_back(i);
    if (rec[i].size() == 2) twos.push_back(i);
  }
  DLOG("rec=", rec);
  DLOG("ones=", ones, "twos=", twos);
  vector<ll> ans;
  ll x = ones[0];
  ans.push_back(x);
  auto [y, z] = rec[x][0];
  if (z == twos[0] || z == twos[1]) swap(y, z);
  ans.push_back(y);
  ans.push_back(z);
  for (ll i = 0; i < n - 3; i++) {
    DLOG("i=", i, "x=", x, "y=", y, "z=", z);
    for (auto [p, q] : rec[z]) {
      DLOG("  p,q=", p, q);
      if ((p == y && q != x) || (q == y && p != x)) {
	if (q == y) swap(p, q);
	DLOG("  pushing", q);
	ans.push_back(q);
	x = y;
	y = z;
	z = q;
	break;
      }
    }
  }
  for (ll i = 0; i < (ll)ans.size(); i++) {
    if (i > 0) cout << " ";
    cout << ans[i] + 1;
  }
  cout << endl;

  return 0;
}

