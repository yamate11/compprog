int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll big = 1e10;

  auto code = [&](char c) -> ll {
    if (c == 'R') return 0;
    if (c == 'L') return 1;
    assert(0);
  };
  vector<ll> cvec4({0,1,2,3});
  auto dec = [&](ll z, ll p) -> ll { return (z >> p) & 1; };

  auto solve = [&]() -> ll {
    ll n; cin >> n;
    string s; cin >> s;
    s = s + s;
    ll ans = big;
    for (ll z = 0; z < 4; z++) {
      vector tbl(n - 1, vector(4, big));
      ll init = dec(z, 1) == code(s[n-2]) ? 0 : 1;
      init   += dec(z, 0) == code(s[n-1]) ? 0 : 1;
      tbl[0][z] = init;
      for (ll i = 0; i < n - 2; i++) {
	for (ll j : cvec4) {
	  for (ll b = 0; b < 2; b++) {
	    if (dec(j, 1) == dec(j, 0) && dec(j, 0) == b) continue;
	    ll ne = (dec(j, 0) << 1) | b;
	    ll flip = b == code(s[i]) ? 0 : 1;
	    updMin(tbl[i + 1][ne], tbl[i][j] + flip);
	  }
	}
      }
      for (ll y = 0; y < 4; y++) {
	if (dec(y, 1) == dec(y, 0) && dec(y, 0) == dec(z, 1)) continue;
	if (dec(y, 0) == dec(z, 1) && dec(z, 1) == dec(z, 0)) continue;
	ans = min(ans, tbl[n - 2][y]);
      }
    }
    return ans;
  };

  ll T; cin >> T;
  for (ll t = 0; t < T; t++) cout << solve() << endl;

  return 0;
}

