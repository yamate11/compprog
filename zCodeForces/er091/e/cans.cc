int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll N, M; cin >> N >> M;
  vector<ll> T(N);
  for (ll i = 0; i < N; i++) {
    ll t; cin >> t; t--;
    T[i] = t;
  }
  vector<ll> z(M), gap(M);
  iota(z.begin(), z.end(), 0);
  vector<set<ll>> pile(M);
  for (ll i = 0; i < N; i++) {
    pile[T[i]].insert(i);
  }
  ll totGap = 0;
  for (ll i = 0; i < M; i++) {
    ll prev = -1;
    for (ll x: pile[i]) {
      if (prev + 1 != x) {
	gap[i]++;
	totGap++;
      }
      prev = x;
    }
  }
  cout << totGap << "\n";
  for (ll i = 0; i < M - 1; i++) {
    ll ax, bx; cin >> ax >> bx; ax--; bx--;
    ll a = z[ax], b = z[bx];
    ll p = a, q = b;
    if (pile[p].size() > pile[q].size()) {
      swap(p, q);
    }
    ll prev = -100;
    auto new_gap = [&](ll x) -> pair<bool, ll> {
      auto it = pile[q].lower_bound(x);
      ll g = -1;
      if (it != pile[q].end()) g = *it;
      if (it == pile[q].begin()) return {true, g};
      it--;
      if (*it + 1 != x) return {true, g};
      return {false, g};
    };
    bool bb = false;
    ll g = -1;
    for (ll x : pile[p]) {
      if (prev + 1 != x) {
	tie(bb, g) = new_gap(x);
	if (x > 0 && bb) {
	  gap[q]++;
	  totGap++;
	}
      }
      pile[q].insert(x);
      if (x + 1 == g) {
	totGap--;
	gap[q]--;
      }
      prev = x;
    }
    totGap -= gap[p];
    gap[p] = 0;
    z[ax] = q;

    DLOG("pile=", pile);
    DLOG("gap=", gap);
    cout << totGap << "\n";

  }
  


  return 0;
}

