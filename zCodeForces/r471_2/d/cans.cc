int naive(istream& cin, ostream& cout) {
  ll n, m, k; cin >> n >> m >> k;
  string s, t; cin >> s >> t;
  string b_ans; cin >> b_ans;
  auto check = [&](string u) -> bool {
    for (ll i = 0; i < (ll)u.size(); i++) {
      if (u.substr(i, m) == t) return true;
    }
    return false;
  };

  if (b_ans == "Yes") {
    ll x, y; cin >> x >> y; x--; y--;
    if (x < 0 || x + k > y || y + k > n) {
      cout << "NG\n";
      return 0;
    }
    string con = s.substr(x, k) + s.substr(y, k);
    if (check(con)) {
      cout << "OK\n";
    }else {
      cout << "NG\n";
    }
    return 0;
  }else if (b_ans == "No") {
    for (ll x = 0; x < n - 2 * k; x++) {
      for (ll y = x + k; y < n - k; y++) {
        if (check(s.substr(x, k) + s.substr(y, k))) {
          cout << "NG\n";
          return 0;
        }
      }
    }
    cout << "OK\n";
    return 0;
  }else {
    cout << "NG\n";
    return 0;
  }

  return 0;
}
int body(istream& cin, ostream& cout) {

  ll n, m, k; cin >> n >> m >> k;
  string s, t; cin >> s >> t;
  
  auto report = [&](ll x, ll y) -> void {
    cout << "Yes\n" << x + 1 << " " << y + 1 << "\n";
  };

  auto analysis = [&](string s1, string t1) -> vector<ll> {
    auto zs = zAlg(t1 + "@" + s1);
    vector<ll> ret(m + 1, -1);
    vector<ll> skip(m + 1);
    iota(skip.begin(), skip.end(), 1);
    for (ll i = 0; i < n; i++) {
      ll len = min(k, (ll)zs[m + 1 + i]);
      if (len == 0) continue;
      ll start = -1;
      if (i < k) {
        if (i + len < k) continue;
        start = k - i;
      }else {
        start = 1;
      }

      ll prev = -1;
      for (ll j = start; j <= len; ) {
        if (ret[j] < 0) {
          ret[j] = i + j;
          DLOGK(i, j);
        }else {
          if (prev >= 1) skip[prev] = skip[j];
        }
        prev = j;
        j = skip[j];
      }
      skip[start] = max(skip[start], len + 1);
    }
    return ret;
  };

  if (m <= k) {
    for (ll i : matches(t, s)) {
      DLOGK(matches(t, s));
      ll i0 = max(0LL, i + m - k);
      if (i0 + k + k <= n) { report(i0, i0 + k); return 0; }
      ll i1 = min(n - k, i);
      if (k <= i1)        { report(0, i1); return 0; }
    }
  }
  auto x = analysis(s, t);
  string srev(s); reverse(srev.begin(), srev.end());
  string trev(t); reverse(trev.begin(), trev.end());
  auto y = analysis(srev, trev);
  
  DLOGK(x);
  DLOGK(y);

  for (ll v = 1; v < m; v++) {
    if (x[v] < 0 || y[m - v] < 0) continue;
    if (x[v] + y[m - v] <= n) { report(x[v] - k, n - y[m - v]); return 0; }
  }
  cout << "No\n";

  return 0;
}

