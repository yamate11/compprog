int main(/* int argc, char *argv[] */) {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout << setprecision(20);

  ll n; cin >> n;
  vector<ll> A(n);
  for (ll i = 0; i < n; i++) cin >> A[i];
  sort(A.begin(), A.end());
  auto check = [&](ll x) -> bool {
    for (ll i = 0; i < x; i++) {
      if (A[i] >= A[n - x + i]) return false;
    }
    return true;
  };
  ll x = binsearch<ll>(check, 0, n);
  cout << x << endl;

  return 0;
}

